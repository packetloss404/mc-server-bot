// ── Interfaces ──────────────────────────────────────────────

export interface SchematicInfo {
  filename: string;
  size: { x: number; y: number; z: number };
  blockCount: number;
  /**
   * Optional per-block-type count map (block name → number of blocks of that
   * type in the schematic). Only populated for schematics small enough to
   * parse without estimation — large schematics (size-estimated or
   * volume > 200k) omit this since we don't iterate the full block list.
   */
  palette?: Record<string, number>;
}

export interface BuildJob {
  id: string;
  schematicFile: string;
  origin: { x: number; y: number; z: number };
  status: 'pending' | 'running' | 'gathering' | 'placing' | 'paused' | 'completed' | 'completed_with_errors' | 'cancelled' | 'failed';
  createdAt: number;
  totalBlocks: number;
  placedBlocks: number;
  assignments: BotAssignment[];
  /** Bot names to remove when the build finishes (created specifically for this build) */
  cleanupBotNames?: string[];
  /**
   * Optional per-bot gather plan generated by the autoGather pre-stage.
   * Populated when `options.autoGather === true`. Empty otherwise.
   */
  gatherPlan?: GatherPlanEntry[];
  /**
   * Aggregate count of blocks that failed placement verification across all
   * assignments. Populated when the build reaches a terminal state so callers
   * can detect partial builds without re-summing assignment counters.
   */
  failedBlockCount?: number;
  /**
   * Snapshot of `placedBlocks` taken when `cancelBuild` fires. Lets operators
   * audit how much of the schematic actually made it into the world before
   * cancellation. Only set on cancelled jobs.
   */
  placedBlocksAtCancel?: number;
  /**
   * Town↔build linkage. When a build is queued by TownBrain on behalf of a
   * planned building row, these carry the owning town + building ids through
   * to the `build:completed` emit so the town registry can flip the row's
   * status (planned → building → complete) and never leaves an orphan that
   * wedges the build loop. Unset for ad-hoc / API-driven builds.
   */
  townId?: string;
  buildingId?: string;
}

/**
 * One queued gather task in the autoGather pre-stage. Each entry corresponds
 * to a single chunk of material the bot needs to fetch before placement.
 */
export interface GatherPlanEntry {
  botName: string;
  resource: string;
  chunkSize: number;
  description: string;
  skillFile: string;
}

export interface BotAssignment {
  botName: string;
  yMin: number;
  yMax: number;
  status: 'waiting' | 'building' | 'completed' | 'failed';
  blocksTotal: number;
  blocksPlaced: number;
  currentY: number;
  /** Count of blocks that failed placement verification after all retry attempts. */
  failedBlocks: number;
}

interface BlockEntry {
  // Store as raw numbers instead of Vec3 to save memory
  wx: number; wy: number; wz: number; // world position
  name: string;
  stateStr: string; // pre-computed block state string
  localY: number;
}

interface CachedSchematic {
  size: { x: number; y: number; z: number };
  /** Blocks in schematic-local coordinates (relative to start). */
  blocks: Array<{ rx: number; ry: number; rz: number; name: string; stateStr: string }>;
}

/**
 * A discovered gather-skill entry. For a given resource (normalized canonical
 * name like `cobblestone`, `oak_log`), pick the LARGEST chunk size available
 * in the skill cache so a shortfall of 32 becomes 4 chunks of 8 rather than
 * 32 chunks of 1.
 */
interface SkillChunk {
  resource: string;
  chunkSize: number;
  skillFile: string;
  /** Original token used for the human description, e.g. "Mine 8 cobblestone". */
  description: string;
}

/**
 * Minecraft 1.18+ overworld build-height range. The world below -64 and above
 * 320 is unbuildable (or doesn't exist), so any origin outside this range is a
 * caller bug and the build is rejected fast instead of clamping silently.
 */
const MIN_BUILD_Y = -64;
const MAX_BUILD_Y = 320;

/** Upper bound on chunks queued across all bots for a single build. */
const AUTOGATHER_MAX_CHUNKS = 50;
/** Polling interval while waiting for bots to clear the inventory bar. */
const AUTOGATHER_POLL_INTERVAL_MS = 5_000;
/** Default upper time bound for the whole gather phase. */
const AUTOGATHER_DEFAULT_TIMEOUT_MS = 10 * 60 * 1000;
/**
 * Default hard deadline for the pre-job phase of startBuild (origin
 * resolution, bunker excavation, site-clear, snap-to-ground combined).
 * Overridden by `config.build.sitePrepTimeoutMs` or by the per-call
 * `options.sitePrepTimeoutMs` knob (lowest wins).
 */
const SITE_PREP_DEFAULT_TIMEOUT_MS = 240_000; // 4 minutes — matches the
// raised SiteSelector deadline (180s) with headroom for the other pre-job
// phases (resolveOrigin, sampleSurfaceY, prepareBunkerSite, optional
// clearSite/snapToGround). Raised from 2.5min when SiteSelector's budget was
// bumped to handle realistic town-scale (19x23+) schematic footprints.

// ── Build Coordinator ───────────────────────────────────────

export class BuildCoordinator {
  private botManager: BotManager;
  private io: SocketIOServer;
  private eventLog: EventLog;
  private jobs: Map<string, BuildJob> = new Map();
  private cancelledJobs: Set<string> = new Set();
  private pausedJobs: Set<string> = new Set();
  private schematicsDir: string;
  private persistPath: string;
  private persistTimer: NodeJS.Timeout | null = null;
  /** Resolved site-prep deadline (from config or the module-level default). */
  private sitePrepTimeoutMs: number;
  /** Original options for each job, kept for resume. */
  private jobOptions = new Map<string, { fillFoundation?: boolean; snapToGround?: boolean; clearSite?: boolean; mode?: 'surface' | 'underground' }>();
  /**
   * Per-job context for underground builds. Captured at startBuild time and
   * consumed after executeBuild resolves to run the bunker entry stairwell.
   */
  private bunkerContext = new Map<string, {
    origin: { x: number; y: number; z: number };
    schSize: { x: number; y: number; z: number };
    surfaceY: number;
    probeBotName: string;
  }>();
  /** Per-job lifecycle hooks supplied by callers (chat dispatcher, etc). */
  private jobHooks = new Map<string, {
    onStarted?: (job: BuildJob) => void;
    onProgress?: (event: { jobId: string; placed: number; total: number; pct: number }) => void;
    onCompleted?: (job: BuildJob) => void;
    /** Track the last milestone we fired (25, 50, 75) so onProgress isn't spammy. */
    lastMilestone: number;
  }>();
  /**
   * Per-job queue of extra blocks that need to be absorbed by an alive bot whose
   * original assignment has completed. Keyed by the receiving bot name.
   */
  private reassignQueues = new Map<string, Map<string, BlockEntry[]>>();
  /** Parsed-schematic cache, keyed by filename. Invalidated when file mtime changes. */
  private schematicCache = new Map<string, { mtimeMs: number; data: CachedSchematic }>();
  /**
   * Per-resource gather-skill catalog. Discovered lazily from `skills/` the
   * first time autoGather runs (and re-discovered if the dir is cleared).
   * Key: normalized resource name (e.g. `cobblestone`, `oak_log`).
   */
  private skillChunkCatalog: Map<string, SkillChunk> | null = null;
  /** Per-job autoGather context, populated when options.autoGather is true. */
  private gatherJobs = new Map<string, { plan: GatherPlanEntry[]; timeoutMs: number }>();
  /** Override for unit tests — when set, skipped wall-clock polling delays. */
  protected gatherPollIntervalMs = AUTOGATHER_POLL_INTERVAL_MS;

  constructor(botManager: BotManager, io: SocketIOServer, eventLog: EventLog, config?: Config) {
    this.botManager = botManager;
    this.io = io;
    this.eventLog = eventLog;
    this.schematicsDir = path.join(process.cwd(), 'schematics');
    this.persistPath = path.join(process.cwd(), 'data', 'builds.json');
    this.sitePrepTimeoutMs =
      config?.build?.sitePrepTimeoutMs ?? SITE_PREP_DEFAULT_TIMEOUT_MS;
    this.loadPersistedJobs();
  }

  // ── Persistence ────────────────────────────────────────

  private loadPersistedJobs(): void {
    try {
      if (!fs.existsSync(this.persistPath)) return;
      const raw = fs.readFileSync(this.persistPath, 'utf-8');
      const data = JSON.parse(raw);
      if (!data || !Array.isArray(data.jobs)) return;
      for (const entry of data.jobs) {
        const job = entry.job as BuildJob;
        // Backfill failedBlocks for jobs persisted before this field existed
        for (const a of job.assignments ?? []) {
          if (typeof a.failedBlocks !== 'number') a.failedBlocks = 0;
        }
        this.jobs.set(job.id, job);
        if (entry.options) this.jobOptions.set(job.id, entry.options);
      }
      logger.info({ count: data.jobs.length }, 'Loaded persisted build jobs');
    } catch (err: any) {
      logger.warn({ err: err.message }, 'Failed to load persisted build jobs');
    }
  }

  private persistJobs(): void {
    try {
      const jobs = [...this.jobs.values()].map((job) => ({
        job,
        options: this.jobOptions.get(job.id) || {},
      }));
      atomicWriteJsonSync(this.persistPath, { jobs });
    } catch (err: any) {
      logger.warn({ err: err.message }, 'Failed to persist build jobs');
    }
  }

  /** Throttled persist — call frequently, writes at most once per 2s. */
  private schedulePersist(): void {
    if (this.persistTimer) return;
    this.persistTimer = setTimeout(() => {
      this.persistTimer = null;
      this.persistJobs();
    }, 2000);
  }

  /**
   * Resume any jobs that were running/paused when the process was last killed.
   * Must be called after the bot workers have had time to connect.
   */
  async resumePendingJobs(): Promise<void> {
    const all = [...this.jobs.values()];
    const toResume = all.filter(
      (j) => j.status === 'running'
        || j.status === 'gathering'
        || j.status === 'placing',
    );
    // Jobs persisted as 'paused' must NOT silently resume building on restart —
    // pausing is an explicit operator action. But the in-memory pause flag and
    // the execution loop are both gone after a restart, so resumeBuild() (which
    // only clears the flag to unblock a *running* loop) couldn't restart them.
    // Re-park them: re-arm pausedJobs and relaunch the loop so it blocks at the
    // pause-wait (placing nothing) and a later resumeBuild() unblocks it.
    const toRepark = all.filter((j) => j.status === 'paused');
    for (const job of toRepark) {
      try {
        logger.info(
          { jobId: job.id, schematic: job.schematicFile, placed: job.placedBlocks, total: job.totalBlocks },
          'Re-parking paused build job (stays paused across restart)',
        );
        await this.resumeJob(job, { keepPaused: true });
      } catch (err: any) {
        logger.error({ jobId: job.id, err: err.message }, 'Failed to re-park paused build job');
        job.status = 'failed';
        this.schedulePersist();
      }
    }
    for (const job of toResume) {
      try {
        logger.info(
          { jobId: job.id, schematic: job.schematicFile, placed: job.placedBlocks, total: job.totalBlocks },
          'Resuming persisted build job',
        );
        await this.resumeJob(job);
      } catch (err: any) {
        logger.error({ jobId: job.id, err: err.message }, 'Failed to resume build job');
        job.status = 'failed';
        this.schedulePersist();
      }
    }
  }

  private async resumeJob(job: BuildJob, opts: { keepPaused?: boolean } = {}): Promise<void> {
    const fullPath = path.join(this.schematicsDir, job.schematicFile);
    if (!fs.existsSync(fullPath)) {
      throw new Error(`Schematic file missing for resume: ${job.schematicFile}`);
    }
    const cached = await this.loadSchematicCached(job.schematicFile);
    const ox = job.origin.x, oy = job.origin.y, oz = job.origin.z;
    const blocks: BlockEntry[] = cached.blocks.map((b) => ({
      wx: ox + b.rx, wy: oy + b.ry, wz: oz + b.rz,
      name: b.name,
      stateStr: b.stateStr,
      localY: b.ry,
    }));

    if (opts.keepPaused) {
      // Re-arm the in-memory pause flag and keep status 'paused'. executeBuild
      // (launched without gatherOpts, so no autoGather re-run) parks at the
      // per-block pause-wait before placing anything; resumeBuild() unblocks it.
      this.pausedJobs.add(job.id);
    } else {
      job.status = 'running';
      this.io.emit('build:started', job);
    }
    this.executeBuild(job.id, blocks, job.assignments).catch((err) => {
      logger.error({ jobId: job.id, err }, 'Resumed build execution failed');
      job.status = 'failed';
      this.io.emit('build:completed', { ...job, error: err.message });
      this.schedulePersist();
    });
  }

  /**
   * Retry a FAILED (or completed_with_errors) build job. Resets its non-
   * completed assignments to runnable and re-executes, resuming each bot from
   * its persisted blocksPlaced so already-placed blocks are skipped. This is
   * how a build that died from transient disconnects/throttle gets driven to
   * completion without re-placing the whole schematic.
   */
  async retryBuild(jobId: string): Promise<BuildJob | null> {
    const job = this.jobs.get(jobId);
    if (!job) return null;
    if (job.status !== 'failed' && job.status !== 'completed_with_errors') {
      throw new Error(
        `Build ${jobId} is '${job.status}' — only failed/completed_with_errors jobs can be retried`,
      );
    }
    for (const a of job.assignments) {
      if (a.status !== 'completed') a.status = 'waiting';
    }
    this.cancelledJobs.delete(jobId);
    logger.info(
      { jobId, placed: job.placedBlocks, total: job.totalBlocks },
      'Retrying build job — resuming from placed blocks',
    );
    await this.resumeJob(job);
    return job;
  }

  /**
   * Demolish a build's footprint with chunked `/fill <bbox> air` issued by an
   * op'd bot. Clears failed / partial / floating ("sky") builds. With
   * `dryRun`, computes and returns the bounding box only (for confirmation)
   * without touching the world. Scoped strictly to THIS job's box — the caller
   * must ensure it doesn't overlap a build worth keeping (it bypasses the
   * mining geofence since /fill is an op command).
   */
  async demolishBuild(
    jobId: string,
    opts: { dryRun?: boolean } = {},
  ): Promise<{
    box: { x1: number; y1: number; z1: number; x2: number; y2: number; z2: number };
    volume: number;
    fillCommands: number;
    executed: boolean;
  } | null> {
    const job = this.jobs.get(jobId);
    if (!job) return null;

    const cached = await this.loadSchematicCached(job.schematicFile);
    const w = cached.size.x, h = cached.size.y, l = cached.size.z;
    const x1 = job.origin.x, y1 = job.origin.y, z1 = job.origin.z;
    const x2 = x1 + w - 1, y2 = y1 + h - 1, z2 = z1 + l - 1;
    const box = { x1, y1, z1, x2, y2, z2 };
    const volume = w * h * l;

    // Chunk into <=32-cube sub-boxes so each /fill stays under the vanilla
    // 32768-block limit regardless of footprint size.
    const CH = 32;
    const chunks: Array<[number, number, number, number, number, number]> = [];
    for (let x = x1; x <= x2; x += CH)
      for (let y = y1; y <= y2; y += CH)
        for (let z = z1; z <= z2; z += CH)
          chunks.push([x, y, z, Math.min(x + CH - 1, x2), Math.min(y + CH - 1, y2), Math.min(z + CH - 1, z2)]);

    if (opts.dryRun) {
      return { box, volume, fillCommands: chunks.length, executed: false };
    }

    // Any connected (op'd) bot can issue the fills.
    let handle: any = null;
    for (const h2 of this.botManager.getAllWorkers() as any[]) {
      if (h2 && typeof h2.isBotConnected === 'function' && (await h2.isBotConnected())) { handle = h2; break; }
    }
    if (!handle) throw new Error('No connected bot available to issue /fill');

    logger.info({ jobId, box, chunks: chunks.length }, 'Demolishing build footprint');
    for (const [cx1, cy1, cz1, cx2, cy2, cz2] of chunks) {
      handle.chat(`/fill ${cx1} ${cy1} ${cz1} ${cx2} ${cy2} ${cz2} air`);
      await this.sleep(150); // gentle cadence so the fills don't trip a chat-spam kick
    }

    job.status = 'cancelled';
    job.placedBlocksAtCancel = job.placedBlocks;
    this.eventLog.push({
      type: 'build:demolished',
      botName: handle.botName ?? 'system',
      description: `Demolished ${job.schematicFile} at ${x1},${y1},${z1} (${chunks.length} /fill ops)`,
      metadata: { jobId, box },
    });
    this.io.emit('build:demolished', { jobId, box });
    this.persistJobs();
    return { box, volume, fillCommands: chunks.length, executed: true };
  }

  /**
   * Build an underground rail+walkway tunnel between the two town halls. An
   * L-shaped stone-shelled corridor at y50: a rail line (powered rail every 8
   * blocks, redstone block beneath to drive carts) along one edge, a 3-wide
   * stone walkway beside it, glowstone-lit ceiling, and ladder shafts up to
   * each hall floor (y64). All via op /fill + /setblock and fully idempotent,
   * so a re-run after a mid-build bot drop just resumes harmlessly. dryRun
   * returns the plan without touching the world.
   */
  async buildTunnel(opts: { dryRun?: boolean } = {}): Promise<{ plan: any; executed: boolean; commands?: number; verify?: { checked: number; repaired: number; missing: number } }> {
    // Geometry: floor block at y50, walkable air y51-55 (5 tall), ceiling y56.
    const FLOOR = 50, AIR1 = 51, AIR2 = 55, CEIL = 56, HALL_Y = 64;
    // Rail centerline forms an L sharing corner block (railX, railZ).
    const railX = 1225, railZ = 122;
    const aZ1 = 122, aZ2 = 526;          // leg A (along Z) toward Hall A
    const bX1 = 1225, bX2 = 1640;        // leg B (along X) toward Hall B
    // Shell + interior boxes (interior 5 wide × 5 tall; shell +1 each side, y49-56).
    const legA = { shell: [1224, 49, 121, 1230, CEIL, 527], air: [1225, AIR1, 122, 1229, AIR2, 526] };
    const legB = { shell: [1224, 49, 121, 1641, CEIL, 127], air: [1225, AIR1, 122, 1640, AIR2, 126] };
    const stairs = [{ x: 1226, z: 524 }, { x: 1638, z: 123 }]; // up into Hall A / Hall B

    const plan = {
      floorY: FLOOR, route: 'L-shaped (shares corner 1225/122)',
      legA_shell: legA.shell, legB_shell: legB.shell,
      rail: { legA: `x${railX} z${aZ1}..${aZ2}`, legB: `z${railZ} x${bX1}..${bX2}` },
      poweredRailEvery: 8, ceilingLightEvery: 5, stairwells: stairs,
    };
    if (opts.dryRun) return { plan, executed: false };

    // Re-acquire a connected op bot if the current one drops mid-build.
    const opBot = async (): Promise<any> => {
      for (const h of this.botManager.getAllWorkers() as any[]) {
        if (h && typeof h.isBotConnected === 'function' && (await h.isBotConnected())) return h;
      }
      return null;
    };
    let handle = await opBot();
    if (!handle) throw new Error('No connected bot available to build the tunnel');

    let n = 0;
    const cmd = async (c: string): Promise<void> => {
      if (n % 60 === 0) { const h = await opBot(); if (h) handle = h; }
      if (!handle) throw new Error('Lost all connected bots mid-tunnel');
      handle.chat(c);
      n++;
      await this.sleep(110);
    };
    const fillBox = async (b: number[], block: string): Promise<void> => {
      const [x1, y1, z1, x2, y2, z2] = b;
      const CH = 32;
      for (let x = x1; x <= x2; x += CH)
        for (let y = y1; y <= y2; y += CH)
          for (let z = z1; z <= z2; z += CH)
            await cmd(`/fill ${x} ${y} ${z} ${Math.min(x + CH - 1, x2)} ${Math.min(y + CH - 1, y2)} ${Math.min(z + CH - 1, z2)} ${block}`);
    };
    // Record the discrete track elements so we can verify+repair them after
    // placement (the bulk stone shell goes in via reliable op /fill and isn't
    // re-read). Names match what getBlockAt returns; stateStr is only used when
    // re-placing a missing block.
    const targets: BlockEntry[] = [];
    const rec = (x: number, y: number, z: number, name: string, stateStr = ''): void => {
      targets.push({ wx: x, wy: y, wz: z, name, stateStr, localY: 0 });
    };

    logger.info({ plan }, 'Building rail+walkway tunnel between town halls');

    // 1) Stone shells, then 2) hollow the interiors (floor at y50 survives).
    await fillBox(legA.shell, 'stone');
    await fillBox(legB.shell, 'stone');
    await fillBox(legA.air, 'air');
    await fillBox(legB.air, 'air');

    // 3) Rail line (sits at y51 on the y50 floor). Powered rail every 8 with a
    //    redstone_block beneath to keep carts moving. Sharing (railX, railZ)
    //    makes the corner auto-curve.
    let i = 0;
    for (let z = aZ2; z >= aZ1; z--, i++) {
      const powered = i % 8 === 0;
      if (powered) { await cmd(`/setblock ${railX} ${FLOOR} ${z} redstone_block`); rec(railX, FLOOR, z, 'redstone_block'); }
      await cmd(`/setblock ${railX} ${AIR1} ${z} ${powered ? 'powered_rail' : 'rail'}`);
      rec(railX, AIR1, z, powered ? 'powered_rail' : 'rail');
    }
    for (let x = bX1; x <= bX2; x++, i++) {
      const powered = i % 8 === 0;
      if (powered) { await cmd(`/setblock ${x} ${FLOOR} ${railZ} redstone_block`); rec(x, FLOOR, railZ, 'redstone_block'); }
      await cmd(`/setblock ${x} ${AIR1} ${railZ} ${powered ? 'powered_rail' : 'rail'}`);
      rec(x, AIR1, railZ, powered ? 'powered_rail' : 'rail');
    }

    // 4) Glowstone in the ceiling for light (no mob spawns).
    for (let z = aZ1; z <= aZ2; z += 5) { await cmd(`/setblock ${railX + 2} ${CEIL} ${z} glowstone`); rec(railX + 2, CEIL, z, 'glowstone'); }
    for (let x = bX1; x <= bX2; x += 5) { await cmd(`/setblock ${x} ${CEIL} ${railZ + 2} glowstone`); rec(x, CEIL, railZ + 2, 'glowstone'); }

    // 5) Ladder shafts up to each hall floor. Ladders face WEST so they attach
    //    to the block to their EAST; through the hollow lower shaft (y51-55)
    //    that block is interior air, so we first lay a stone backing pillar
    //    there (above the ceiling the ladder attaches to solid terrain). The
    //    east side is used so the backing never collides with the rail line
    //    (legA rail at x1225, legB rail at z122). Without this the bottom rungs
    //    had no support and popped off → unenterable shaft.
    for (const s of stairs) {
      await fillBox([s.x, AIR1, s.z, s.x, HALL_Y, s.z], 'air');
      for (let y = AIR1; y <= AIR2; y++) { await cmd(`/setblock ${s.x + 1} ${y} ${s.z} stone`); rec(s.x + 1, y, s.z, 'stone'); }
      for (let y = AIR1; y <= HALL_Y; y++) { await cmd(`/setblock ${s.x} ${y} ${s.z} ladder[facing=west]`); rec(s.x, y, s.z, 'ladder', 'facing=west'); }
    }

    // 6) Verify the track elements landed and repair any that didn't.
    const sweep = await this.verifyAndRepairTargets(null, targets, []);
    logger.info(
      { checked: sweep.checked, repaired: sweep.repaired, missing: sweep.missing, sample: sweep.sampleMisses.slice(0, 8) },
      'Tunnel verify-and-repair sweep complete',
    );

    this.eventLog.push({
      type: 'build:tunnel',
      botName: handle?.botName ?? 'system',
      description: `Built rail+walkway tunnel between town halls (${n} commands, ${sweep.missing} track blocks missing after verify)`,
      metadata: { plan, verify: { checked: sweep.checked, repaired: sweep.repaired, missing: sweep.missing } },
    });
    this.io.emit('build:tunnel', { plan });
    logger.info({ commands: n, verify: { checked: sweep.checked, repaired: sweep.repaired, missing: sweep.missing } }, 'Tunnel build complete');
    return { plan, executed: true, commands: n, verify: { checked: sweep.checked, repaired: sweep.repaired, missing: sweep.missing } };
  }

  // ── Schematic parsing & cache ──────────────────────────

  /**
   * Load a schematic, returning blocks in schematic-local coords.
   * Result is cached by filename+mtime so repeated startBuild/resumeJob calls
   * for the same file skip the NBT parse + triple-nested iteration.
   */
  private async loadSchematicCached(filename: string): Promise<CachedSchematic> {
    const fullPath = path.join(this.schematicsDir, filename);
    const mtimeMs = fs.statSync(fullPath).mtimeMs;
    const cached = this.schematicCache.get(filename);
    if (cached && cached.mtimeMs === mtimeMs) return cached.data;

    const { Schematic } = require('prismarine-schematic');
    const buffer = fs.readFileSync(fullPath);
    const schematic = await Schematic.read(buffer, await this.getBotVersion());
    const start = schematic.start();
    const end = schematic.end();
    const sx = start.x, sy = start.y, sz = start.z;
    const size = schematic.size;

    const blocks: CachedSchematic['blocks'] = [];
    const tempPos = new Vec3(0, 0, 0);
    for (let y = start.y; y <= end.y; y++) {
      for (let z = start.z; z <= end.z; z++) {
        for (let x = start.x; x <= end.x; x++) {
          tempPos.x = x; tempPos.y = y; tempPos.z = z;
          const block = schematic.getBlock(tempPos);
          if (block && block.name !== 'air' && block.name !== 'cave_air' && block.name !== 'void_air') {
            const props = block.getProperties ? block.getProperties() : {};
            const stateStr = Object.entries(props).map(([k, v]) => `${k}=${v}`).join(',');
            blocks.push({ rx: x - sx, ry: y - sy, rz: z - sz, name: block.name, stateStr });
          }
        }
      }
    }

    const data: CachedSchematic = { size: { x: size.x, y: size.y, z: size.z }, blocks };
    this.schematicCache.set(filename, { mtimeMs, data });
    return data;
  }

  // ── Schematic listing ───────────────────────────────────

  private async getBotVersion(): Promise<string> {
    const bots = this.botManager.getAllWorkers() as any[];
    for (const b of bots) {
      if (typeof b.getBotVersion === 'function') {
        const v = await b.getBotVersion();
        if (v) return v;
      }
    }
    return '1.21.11';
  }

  async listSchematics(): Promise<SchematicInfo[]> {
    if (!fs.existsSync(this.schematicsDir)) return [];

    const files = fs.readdirSync(this.schematicsDir).filter(
      (f) => f.endsWith('.schem') || f.endsWith('.schematic'),
    );

    const results: SchematicInfo[] = [];
    for (const filename of files) {
      try {
        // Skip files larger than 1MB to avoid OOM on huge schematics
        const filePath = path.join(this.schematicsDir, filename);
        const stat = fs.statSync(filePath);
        if (stat.size > 10_000_000) {
          results.push({ filename, size: { x: 0, y: 0, z: 0 }, blockCount: 0 });
          logger.info({ filename, sizeBytes: stat.size }, 'Skipping large schematic metadata load');
          continue;
        }
        const info = await this.getSchematicInfoSafe(filename);
        if (info) results.push(info);
      } catch (err: any) {
        logger.warn({ filename, err: err.message }, 'Failed to read schematic metadata');
        results.push({ filename, size: { x: 0, y: 0, z: 0 }, blockCount: 0 });
      }
    }
    return results;
  }

  /** Safe metadata loader — gets dimensions without holding the full schematic in memory */
  private async getSchematicInfoSafe(filename: string): Promise<SchematicInfo | null> {
    const fullPath = path.join(this.schematicsDir, filename);
    if (!fs.existsSync(fullPath)) return null;

    // Heuristic: compressed .schem files typically have ~100:1 to ~200:1 ratio.
    // Files over 50KB compressed likely decompress to millions of voxels — skip parsing entirely.
    const fileSize = fs.statSync(fullPath).size;
    if (fileSize > 50_000) {
      logger.info({ filename, fileSize }, 'Large schematic — estimating dimensions from file size');
      // Rough estimate: compressed size * 150 gives approx total voxels, cube-root for dimensions
      const estVoxels = fileSize * 150;
      const estDim = Math.round(Math.cbrt(estVoxels));
      return { filename, size: { x: estDim, y: Math.round(estDim * 0.6), z: estDim }, blockCount: Math.round(estVoxels * 0.15) };
    }

    try {
      const cached = await this.loadSchematicCached(filename);
      const size = cached.size;
      const volume = size.x * size.y * size.z;
      // For very large schematics, estimate block count and skip iteration
      if (volume > 200_000) {
        return { filename, size, blockCount: Math.round(volume * 0.15) };
      }
      // Tally palette counts so the dashboard can render a material list.
      // Cheap: cached.blocks is already in memory, this is one pass.
      const palette: Record<string, number> = {};
      for (const b of cached.blocks) {
        palette[b.name] = (palette[b.name] ?? 0) + 1;
      }
      return { filename, size, blockCount: cached.blocks.length, palette };
    } catch (err: any) {
      logger.warn({ filename, err: err.message }, 'Failed to parse schematic');
      return { filename, size: { x: 0, y: 0, z: 0 }, blockCount: 0 };
    }
  }

  async getSchematicInfoAsync(filename: string): Promise<SchematicInfo | null> {
    return this.getSchematicInfoSafe(filename);
  }

  // ── Build job management ────────────────────────────────

  async startBuild(
    schematicFile: string,
    origin: { x: number; y: number; z: number },
    botNames: string[],
    options?: {
      cleanupBotNames?: string[];
      fillFoundation?: boolean;
      snapToGround?: boolean;
      clearSite?: boolean;
      /**
       * How to resolve the build origin.
       *  - 'coords'      (default): use the supplied `origin` directly
       *  - 'auto-flat'   : ignore origin, call SiteSelector to find a flat spot
       *                    near the probe bot
       *  - 'bot:<name>'  : place at the named bot's current position
       *  - 'player:<name>': place at the named player's position (resolved via
       *                    PlayerPresenceTracker if a recent location is known)
       */
      originMode?: 'coords' | 'auto-flat' | `bot:${string}` | `player:${string}`;
      /**
       * Build placement mode.
       *  - 'surface' (default): place the schematic at the resolved origin Y.
       *  - 'underground': resample the surface, push origin Y below it so the
       *    schematic top sits at surface_y - 2 (leaving a soil cap), excavate
       *    the pit, and build a stairwell entrance after placement completes.
       */
      mode?: 'surface' | 'underground';
      /**
       * Lifecycle hooks (in-process — not Socket.IO). Used by the chat
       * dispatcher to make the bot announce progress to the player who asked
       * for the build. All hooks are best-effort; thrown errors are caught
       * and logged.
       */
      onStarted?: (job: BuildJob) => void;
      onProgress?: (event: { jobId: string; placed: number; total: number; pct: number }) => void;
      onCompleted?: (job: BuildJob) => void;
      /**
       * When true, before starting placement the coordinator computes the
       * schematic's per-material requirement, inspects each assigned bot's
       * cached inventory, queues gather sub-tasks (mine/craft) on the bots
       * for any shortfall, and waits for inventories to clear the bar.
       * Defaults to false — legacy behavior.
       */
      autoGather?: boolean;
      /**
       * How long to wait for the gather pre-stage to satisfy the inventory
       * bar before the build is failed. Defaults to 10 minutes.
       */
      autoGatherTimeoutMs?: number;
      /**
       * Town↔build linkage (see BuildJob.townId/buildingId). When TownBrain
       * queues a build for a planned building row it passes these so the
       * resulting job — and its `build:completed` emit — carries the ids back
       * to the town registry.
       */
      townId?: string;
      buildingId?: string;
      /**
       * Per-call override for the pre-job site-prep deadline. Overrides both
       * the config knob and the module-level default. Useful in tests and for
       * callers that know the site prep will be unusually slow (e.g. very large
       * bunker excavations). When omitted, the constructor-resolved timeout
       * (`config.build.sitePrepTimeoutMs` ?? 150 s) is used.
       */
      sitePrepTimeoutMs?: number;
    },
  ): Promise<BuildJob> {
    const fullPath = path.join(this.schematicsDir, schematicFile);

    if (!fs.existsSync(fullPath)) {
      throw new Error(`Schematic file not found: ${schematicFile}`);
    }

    // ── Origin Y validation (pre-resolveOrigin) ──
    // Reject obviously bad origins from the caller up front. For 'coords' mode
    // resolveOrigin returns this as-is, so the check is authoritative; for
    // 'auto-flat' / 'bot:*' / 'player:*' modes resolveOrigin may produce a
    // different Y, so we re-check post-resolution below.
    const assertOriginYInRange = (o: { x: number; y: number; z: number }, phase: string) => {
      if (
        !Number.isFinite(o.y) ||
        o.y < MIN_BUILD_Y ||
        o.y > MAX_BUILD_Y
      ) {
        throw new Error(
          `Origin Y out of build-height range (${phase}): ${o.y} (must be between ${MIN_BUILD_Y} and ${MAX_BUILD_Y})`,
        );
      }
    };
    assertOriginYInRange(origin, 'supplied');

    // Validate bots exist and are connected (via worker IPC)
    for (const name of botNames) {
      const handle = this.botManager.getWorker(name) as any;
      if (!handle) throw new Error(`Bot not found: ${name}`);
      const connected = await handle.isBotConnected();
      if (!connected) throw new Error(`Bot not connected: ${name}`);
    }

    // Check file size before loading to prevent OOM
    const fileStat = fs.statSync(fullPath);
    if (fileStat.size > 10_000_000) {
      throw new Error(`Schematic file too large (${(fileStat.size / 1_000_000).toFixed(1)}MB). Max file size: 10MB.`);
    }

    const cached = await this.loadSchematicCached(schematicFile);
    const schSize = cached.size;
    const volume = schSize.x * schSize.y * schSize.z;
    if (volume > 2_000_000) {
      throw new Error(`Schematic volume too large (${schSize.x}x${schSize.y}x${schSize.z} = ${volume.toLocaleString()} voxels). Max 2M voxels.`);
    }

    // ── Options ──
    let fillFoundation = options?.fillFoundation !== false; // default true
    let snapToGround = options?.snapToGround === true; // default false
    let clearSiteEnabled = options?.clearSite !== false; // default true
    const originMode = options?.originMode ?? 'coords';

    // Auto-promote bunker-named schematics to underground mode when caller
    // hasn't specified `mode` explicitly. Lets builds dropped via name alone
    // do the right thing without a UI tweak.
    let mode: 'surface' | 'underground' = options?.mode ?? 'surface';
    if (!options?.mode && /bunker|tunnel|vault/i.test(schematicFile)) {
      logger.warn(
        { schematicFile },
        'Schematic name matches bunker/tunnel/vault — auto-promoting to underground mode',
      );
      mode = 'underground';
    }

    // Underground mode disables surface-mode site prep entirely. The pit
    // excavation in prepareBunkerSite replaces clearSite + fillFoundation,
    // and the resampled surface Y replaces snapToGround.
    if (mode === 'underground') {
      fillFoundation = false;
      snapToGround = false;
      clearSiteEnabled = false;
    }

    // ── Pre-job site-prep deadline ──
    // The PRE-JOB phase (everything before `executeBuild` is kicked off) runs
    // several slow awaits while holding the TownBrain tick-lock. If any of
    // them hangs the whole town-management loop freezes until restart.
    //
    // Strategy: track a shared deadline. CRITICAL awaits (resolveOrigin,
    // prepareBunkerSite) get the remaining budget — a timeout there propagates
    // and lets TownBrain clean up and retry. BEST-EFFORT awaits (runClearSite,
    // snapToGround) get the remaining budget too, but are already wrapped in
    // try/catch that continues on error — a timeout just becomes another
    // caught error with a log line, preserving the graceful-degrade semantics.
    const sitePrepBudgetMs =
      options?.sitePrepTimeoutMs ?? this.sitePrepTimeoutMs;
    const sitePrepDeadline = Date.now() + sitePrepBudgetMs;
    /** Remaining ms until the pre-job deadline (always >= 1). */
    const sitePrepRemaining = (): number =>
      Math.max(1, sitePrepDeadline - Date.now());

    // Find the connected bot CLOSEST to the supplied origin as the probe.
    // `bot.blockAt()` returns null for chunks outside the bot's view distance
    // (~128-192 blocks), so a far-away probe makes every getBlockAt call read
    // null — SiteSelector then can't find any solid ground and times out.
    // Iterating in resident-list order would've used Greta (alphabetically
    // first), who could be on the other side of the world from the build
    // site. Pick by distance instead so the probe's chunks already cover the
    // search area.
    let probeHandle: any = null;
    let bestProbeDist = Infinity;
    for (const n of botNames) {
      const h = this.botManager.getWorker(n) as any;
      if (!h || typeof h.isBotConnected !== 'function') continue;
      if (!(await h.isBotConnected())) continue;
      const pos = h.getCachedStatus?.()?.position;
      if (!pos) {
        // Fall back to using a bot with unknown position only if nothing
        // closer turns up — better than nothing.
        if (probeHandle === null) probeHandle = h;
        continue;
      }
      const d = Math.hypot(pos.x - origin.x, pos.z - origin.z);
      if (d < bestProbeDist) {
        bestProbeDist = d;
        probeHandle = h;
      }
    }

    // ── Origin resolution ──
    // Map originMode -> concrete {x, y, z}. The supplied `origin` is the
    // fallback for 'coords' mode and a hint elsewhere (e.g. Y for player mode).
    // CRITICAL: propagate a timeout so the caller can clean up.
    origin = await withTimeout(
      this.resolveOrigin(originMode, origin, botNames, probeHandle, schSize),
      sitePrepRemaining(),
      'startBuild resolveOrigin',
    );
    // resolveOrigin can return a Y from bot position, player position, or
    // auto-flat site selection — none of which are guaranteed to land inside
    // the build-height range. Re-check after resolution so non-'coords' modes
    // can't slip past the early gate.
    assertOriginYInRange(origin, `resolved (${originMode})`);

    // ── Underground mode: resample surface and excavate the pit ──
    // Must run AFTER origin resolution (we need the chosen x,z) but BEFORE
    // foundation fill / snap-to-ground, which would otherwise undo the pit.
    let bunkerSurfaceY: number | null = null;
    if (mode === 'underground') {
      if (!probeHandle) {
        throw new Error('Underground build requires a connected bot to excavate the pit');
      }
      // CRITICAL: both sampleSurfaceY and prepareBunkerSite are slow IPC/dig
      // ops — wrap each with the remaining site-prep budget so a hang here
      // propagates and lets the caller clean up rather than freezing the loop.
      const surfaceY = await withTimeout(
        sampleSurfaceY(probeHandle, origin.x, origin.z),
        sitePrepRemaining(),
        'startBuild sampleSurfaceY',
      );
      if (surfaceY == null) {
        throw new Error(
          `Underground build: could not sample surface Y around (${origin.x}, ${origin.z})`,
        );
      }
      // Place the schematic so its TOP block sits at surface_y - 2, leaving a
      // one-block soil cap (the block at surface_y - 1) intact above it.
      const desiredOriginY = surfaceY - schSize.y - 1;
      logger.info(
        { surfaceY, oldOriginY: origin.y, newOriginY: desiredOriginY, schSize },
        'Underground mode: shifting origin Y below resampled surface',
      );
      origin = { x: origin.x, y: desiredOriginY, z: origin.z };
      bunkerSurfaceY = surfaceY;

      // Excavate the pit. Throws if bedrock or nearby liquid would break the build.
      // CRITICAL: propagate a timeout so the caller can clean up.
      const bunkerResult = await withTimeout(
        prepareBunkerSite(probeHandle, origin, schSize),
        sitePrepRemaining(),
        'startBuild prepareBunkerSite',
      );
      logger.info(
        { excavated: bunkerResult.excavated, warnings: bunkerResult.warnings.length },
        'Underground mode: pit excavated',
      );
    }

    const ox = origin.x, oy = origin.y, oz = origin.z;
    const blocks: BlockEntry[] = cached.blocks.map((b) => ({
      wx: ox + b.rx, wy: oy + b.ry, wz: oz + b.rz,
      name: b.name,
      stateStr: b.stateStr,
      localY: b.ry,
    }));

    if (blocks.length === 0) {
      throw new Error('Schematic contains no blocks');
    }

    // ── Pre-build site-prep: clear the footprint before any placement ──
    // Runs BEFORE snap-to-ground because leveling/terrain probes should see
    // a clean slate (no trees, no grass tufts, no water interfering).
    if (clearSiteEnabled && probeHandle) {
      const footprintMin = {
        x: origin.x,
        y: origin.y,
        z: origin.z,
      };
      const footprintMax = {
        x: origin.x + schSize.x - 1,
        y: origin.y + schSize.y - 1,
        z: origin.z + schSize.z - 1,
      };
      try {
        // Extend clearance high enough to take out tree canopies that overhang
        // the schematic top. Typical trees in vanilla cap around 8 blocks of
        // trunk + leaves; 12 is a safe floor for any schematic shorter than that.
        const clearanceHeight = Math.max(schSize.y, 12);
        // BEST-EFFORT: a timeout here is caught by the outer try/catch just
        // like any other runClearSite failure — we log and continue.
        const clearResult = await withTimeout(
          this.runClearSite(probeHandle, {
            footprintMin,
            footprintMax,
            clearanceHeight,
          }),
          sitePrepRemaining(),
          'startBuild runClearSite',
        );
        logger.info(
          { clearedSlabs: clearResult.cleared, skippedProtected: clearResult.skipped, errors: clearResult.errors.length, clearanceHeight },
          'Site-prep: cleared footprint ahead of snap-to-ground',
        );
      } catch (err: any) {
        logger.warn({ err: err.message }, 'Site-prep: clear failed — continuing with build');
      }
    } else if (clearSiteEnabled && !probeHandle) {
      logger.warn('Site-prep requested but no connected bot available to issue /fill');
    }

    if (snapToGround && probeHandle) {
      // BEST-EFFORT: wrap the whole snap-to-ground probe loop with the remaining
      // site-prep budget. A timeout (or any other error) is caught and logged,
      // then we continue without snapping — identical to what happens today when
      // no samples are available.
      try {
        await withTimeout(
          (async () => {
            // Build a grid of sample points across the footprint
            const footprintXZ = new Map<string, { wx: number; wz: number }>();
            for (const b of blocks) {
              const key = `${b.wx},${b.wz}`;
              if (!footprintXZ.has(key)) footprintXZ.set(key, { wx: b.wx, wz: b.wz });
            }

            // Sample up to 50 evenly-spaced columns. Probe columns in parallel chunks
            // so we issue ~10 concurrent IPC scans instead of one at a time.
            const allColumns = [...footprintXZ.values()];
            const sampleStep = Math.max(1, Math.floor(allColumns.length / 50));
            const sampledColumns: { wx: number; wz: number }[] = [];
            for (let i = 0; i < allColumns.length; i += sampleStep) {
              sampledColumns.push(allColumns[i]);
            }
            const samples: number[] = [];
            const PROBE_CONCURRENCY = 10;
            for (let i = 0; i < sampledColumns.length; i += PROBE_CONCURRENCY) {
              const chunk = sampledColumns.slice(i, i + PROBE_CONCURRENCY);
              const results = await Promise.all(chunk.map(async (col) => {
                for (let y = oy + 10; y >= oy - 30; y--) {
                  const wb = await probeHandle.getBlockAt(col.wx, y, col.wz);
                  if (wb && wb.name !== 'air' && wb.name !== 'cave_air' && wb.name !== 'void_air') {
                    return y + 1;
                  }
                }
                return null;
              }));
              for (const r of results) {
                if (r !== null) samples.push(r);
              }
            }

            if (samples.length > 0) {
              // Use median ground level
              samples.sort((a, b) => a - b);
              const medianGround = samples[Math.floor(samples.length / 2)];
              const diff = medianGround - oy;
              if (diff !== 0) {
                const big = Math.abs(diff) > 5;
                logger.info(
                  { oldY: oy, newY: medianGround, diff, samples: samples.length, largeDelta: big },
                  big
                    ? 'Snap-to-ground: large terrain delta — still adjusting (fillFoundation will plug gaps)'
                    : 'Snap-to-ground: adjusting origin Y to median terrain height',
                );
                // Shift all block world-Y positions by the difference. Previously we
                // refused to adjust when |diff| > 5, which left builds floating in
                // mid-air over hills/valleys. Trust fillFoundation to plug any gap.
                for (const b of blocks) {
                  b.wy += diff;
                }
                origin.y = medianGround;
              }
            }
          })(),
          sitePrepRemaining(),
          'startBuild snapToGround',
        );
      } catch (err: any) {
        logger.warn({ err: err.message }, 'Snap-to-ground: probe failed — continuing without snap');
      }
    }

    // ── Foundation filling: add support blocks under schematic footprint ──
    if (fillFoundation && probeHandle) {
      // Bumped from 20 -> 32: snap-to-ground now accepts large terrain deltas,
      // so the fill column may need to reach further down to find solid ground.
      const MAX_FILL_DEPTH = 32;

      // For each (wx, wz) column, find the lowest schematic block Y
      const columnMinY = new Map<string, number>();
      for (const b of blocks) {
        const key = `${b.wx},${b.wz}`;
        const prev = columnMinY.get(key);
        if (prev === undefined || b.wy < prev) {
          columnMinY.set(key, b.wy);
        }
      }

      const foundationBlocks: BlockEntry[] = [];
      let skippedLiquid = 0;
      let skippedOutOfRange = 0;

      const columnEntries = [...columnMinY.entries()];
      const FOUNDATION_CONCURRENCY = 10;
      for (let ci = 0; ci < columnEntries.length; ci += FOUNDATION_CONCURRENCY) {
        const chunk = columnEntries.slice(ci, ci + FOUNDATION_CONCURRENCY);
        const results = await Promise.all(chunk.map(async ([key, minBlockY]) => {
          const [wxStr, wzStr] = key.split(',');
          const wx = parseInt(wxStr, 10);
          const wz = parseInt(wzStr, 10);
          const fills: BlockEntry[] = [];
          let outcome: 'ok' | 'liquid' | 'out_of_range' = 'ok';
          let liquidName = '';
          let liquidY = 0;
          for (let y = minBlockY - 1; y >= minBlockY - MAX_FILL_DEPTH; y--) {
            const wb = await probeHandle.getBlockAt(wx, y, wz);
            if (!wb) { outcome = 'out_of_range'; break; }
            if (wb.name === 'air' || wb.name === 'cave_air' || wb.name === 'void_air') {
              fills.push({
                wx, wy: y, wz,
                name: 'stone',
                stateStr: '',
                localY: -1 - fills.length,
              });
            } else if (wb.name === 'water' || wb.name === 'lava'
              || wb.name === 'flowing_water' || wb.name === 'flowing_lava') {
              outcome = 'liquid'; liquidName = wb.name; liquidY = y; break;
            } else {
              break;
            }
          }
          return { wx, wz, fills, outcome, liquidName, liquidY };
        }));
        for (const r of results) {
          if (r.outcome === 'out_of_range') {
            skippedOutOfRange++;
          } else if (r.outcome === 'liquid') {
            logger.warn(
              { wx: r.wx, y: r.liquidY, wz: r.wz, liquid: r.liquidName },
              'Foundation fill: liquid detected under schematic, skipping column',
            );
            skippedLiquid++;
          }
          for (const f of r.fills) foundationBlocks.push(f);
        }
      }

      if (foundationBlocks.length > 0) {
        logger.info(
          { foundationBlocks: foundationBlocks.length, columns: columnMinY.size, skippedLiquid, skippedOutOfRange },
          'Foundation fill: adding support blocks under schematic',
        );
        // Sort foundation blocks bottom-up so they build from ground level
        foundationBlocks.sort((a, b) => a.wy - b.wy);
        // Prepend foundation blocks before schematic blocks
        blocks.unshift(...foundationBlocks);
      } else {
        logger.info('Foundation fill: no gaps detected, no fill needed');
      }
    } else if (fillFoundation && !probeHandle) {
      logger.warn('Foundation fill requested but no connected bot available to probe terrain');
    }

    // Safety limit on actual block count to prevent OOM
    const MAX_BLOCKS = 500000;
    if (blocks.length > MAX_BLOCKS) {
      throw new Error(`Schematic has ${blocks.length.toLocaleString()} blocks. Max supported: ${MAX_BLOCKS.toLocaleString()}.`);
    }

    // Determine Y range
    const minLocalY = 0;
    const maxLocalY = schSize.y - 1;

    // Partition by block count so each bot gets roughly equal work
    // Group blocks by Y layer, then assign layers to bots greedily
    const blocksPerY = new Map<number, number>();
    for (const b of blocks) {
      blocksPerY.set(b.localY, (blocksPerY.get(b.localY) || 0) + 1);
    }
    const yLevels = [...blocksPerY.keys()].sort((a, b) => a - b);
    const targetPerBot = Math.ceil(blocks.length / botNames.length);

    // Assign contiguous Y ranges to each bot, splitting when block count target is reached
    const botRanges: { yMin: number; yMax: number; count: number }[] = [];
    let currentCount = 0;
    let rangeStart = yLevels[0] ?? 0;

    for (let i = 0; i < yLevels.length; i++) {
      currentCount += blocksPerY.get(yLevels[i])!;
      const isLast = i === yLevels.length - 1;
      const reachedTarget = currentCount >= targetPerBot && botRanges.length < botNames.length - 1;

      if (reachedTarget || isLast) {
        botRanges.push({ yMin: rangeStart, yMax: yLevels[i], count: currentCount });
        currentCount = 0;
        if (i < yLevels.length - 1) rangeStart = yLevels[i + 1];
      }
    }

    // If fewer ranges than bots (very flat schematic), only use needed bots
    const assignments: BotAssignment[] = botNames.slice(0, botRanges.length).map((botName, idx) => {
      const range = botRanges[idx];
      const botBlocks = blocks.filter((b) => b.localY >= range.yMin && b.localY <= range.yMax);
      return {
        botName,
        yMin: range.yMin,
        yMax: range.yMax,
        status: 'waiting' as const,
        blocksTotal: botBlocks.length,
        blocksPlaced: 0,
        currentY: range.yMin,
        failedBlocks: 0,
      } as BotAssignment;
    });

    logger.info(
      { assignments: assignments.map(a => ({ bot: a.botName, yMin: a.yMin, yMax: a.yMax, blocks: a.blocksTotal })) },
      'Build work partitioned by block count',
    );

    const jobId = crypto.randomUUID();
    const job: BuildJob = {
      id: jobId,
      schematicFile,
      origin,
      status: 'running',
      createdAt: Date.now(),
      totalBlocks: blocks.length,
      placedBlocks: 0,
      assignments,
      cleanupBotNames: options?.cleanupBotNames,
      townId: options?.townId,
      buildingId: options?.buildingId,
    };

    this.jobs.set(jobId, job);
    this.jobOptions.set(jobId, {
      fillFoundation: options?.fillFoundation,
      snapToGround: options?.snapToGround,
      clearSite: options?.clearSite,
      mode,
    });
    // Stash autoGather inputs for executeBuild to consume.
    const autoGather = options?.autoGather === true;
    const autoGatherTimeoutMs = options?.autoGatherTimeoutMs ?? AUTOGATHER_DEFAULT_TIMEOUT_MS;
    // Stash the data runBunkerEntry will need after executeBuild resolves.
    if (mode === 'underground' && bunkerSurfaceY != null && probeHandle) {
      // Find which botName our probeHandle corresponds to so we can re-resolve
      // it later (probeHandle may have died or reconnected by then).
      let probeBotName = botNames[0];
      for (const n of botNames) {
        const h = this.botManager.getWorker(n) as any;
        if (h === probeHandle) { probeBotName = n; break; }
      }
      this.bunkerContext.set(jobId, {
        origin: { x: origin.x, y: origin.y, z: origin.z },
        schSize,
        surfaceY: bunkerSurfaceY,
        probeBotName,
      });
    }
    this.persistJobs();

    // Stash lifecycle hooks before any callbacks fire.
    if (options?.onStarted || options?.onProgress || options?.onCompleted) {
      this.jobHooks.set(jobId, {
        onStarted: options.onStarted,
        onProgress: options.onProgress,
        onCompleted: options.onCompleted,
        lastMilestone: 0,
      });
    }
    try { options?.onStarted?.(job); } catch (err: any) {
      logger.warn({ err: err.message, jobId }, 'onStarted hook threw');
    }

    // Emit started event
    this.io.emit('build:started', job);
    this.eventLog.push({
      type: 'build:started',
      botName: botNames.join(', '),
      description: `Build started: ${schematicFile} with ${botNames.length} bot(s)`,
      metadata: { jobId, schematicFile, origin, botNames },
    });

    logger.info(
      { jobId, schematicFile, origin, bots: botNames, totalBlocks: blocks.length },
      'Multi-bot build started',
    );

    // Start execution in background (non-blocking)
    this.executeBuild(jobId, blocks, assignments, { autoGather, autoGatherTimeoutMs, botNames }).catch((err) => {
      logger.error({ jobId, err }, 'Build execution failed');
      job.status = 'failed';
      // Fire the onCompleted hook on this failure path too. The normal
      // completion path (executeBuild's terminal block) runs the hook, but a
      // throw here bypassed it — without this the town linkage would never
      // learn the build failed and the building row would stay 'building'
      // forever, re-wedging the build loop.
      const hooks = this.jobHooks.get(jobId);
      if (hooks?.onCompleted) {
        try { hooks.onCompleted(job); } catch (hookErr: any) {
          logger.warn({ err: hookErr.message, jobId }, 'onCompleted hook threw (failure path)');
        }
      }
      this.jobHooks.delete(jobId);
      this.io.emit('build:completed', { ...job, error: err.message });
    });

    return job;
  }

  cancelBuild(jobId: string): boolean {
    const job = this.jobs.get(jobId);
    if (!job || job.status === 'completed' || job.status === 'completed_with_errors' || job.status === 'cancelled') return false;

    this.cancelledJobs.add(jobId);
    job.status = 'cancelled';
    // Snapshot block-count at cancel time so an operator can audit how much of
    // the schematic actually made it into the world. We deliberately do NOT
    // try to roll back placed blocks — that's a separate (out-of-scope)
    // undo system. Stash on both the job (for /api/builds/:id) and the event
    // metadata so operators have one number to look at either way.
    const placedAtCancel = job.placedBlocks;
    job.placedBlocksAtCancel = placedAtCancel;
    this.schedulePersist();

    // Reset bot states
    for (const assignment of job.assignments) {
      if (assignment.status === 'building' || assignment.status === 'waiting') {
        assignment.status = 'failed';
        const handle = this.botManager.getWorker(assignment.botName) as any;
        if (handle && typeof handle.setBotState === 'function') handle.setBotState(BotState.IDLE);
      }
    }

    // Same delayed eviction as natural completion — a cancelled job's body
    // (assignments + per-block arrays) still pins memory until evicted.
    this.scheduleTerminalJobEviction(jobId);

    this.io.emit('build:cancelled', { jobId, placedBlocksAtCancel: placedAtCancel, totalBlocks: job.totalBlocks });
    this.eventLog.push({
      type: 'build:cancelled',
      botName: job.assignments.map((a) => a.botName).join(', '),
      description: `Build cancelled: ${job.schematicFile} (${placedAtCancel}/${job.totalBlocks} blocks placed)`,
      metadata: { jobId, placedBlocksAtCancel: placedAtCancel, totalBlocks: job.totalBlocks },
    });

    logger.info(
      { jobId, placedBlocksAtCancel: placedAtCancel, totalBlocks: job.totalBlocks },
      'Build cancelled — partial placement left in world for operator audit',
    );
    return true;
  }

  pauseBuild(jobId: string): boolean {
    const job = this.jobs.get(jobId);
    if (!job || job.status !== 'running') return false;

    this.pausedJobs.add(jobId);
    job.status = 'paused';
    this.schedulePersist();

    this.io.emit('build:bot-status', { jobId, status: 'paused' });
    logger.info({ jobId }, 'Build paused');
    return true;
  }

  resumeBuild(jobId: string): boolean {
    const job = this.jobs.get(jobId);
    if (!job || job.status !== 'paused') return false;

    this.pausedJobs.delete(jobId);
    job.status = 'running';
    this.schedulePersist();

    this.io.emit('build:bot-status', { jobId, status: 'running' });
    logger.info({ jobId }, 'Build resumed');
    return true;
  }

  getBuildJob(jobId: string): BuildJob | undefined {
    return this.jobs.get(jobId);
  }

  getAllBuildJobs(): BuildJob[] {
    return [...this.jobs.values()];
  }

  // ── Execution engine ────────────────────────────────────

  private async executeBuild(
    jobId: string,
    blocks: BlockEntry[],
    assignments: BotAssignment[],
    gatherOpts?: { autoGather?: boolean; autoGatherTimeoutMs?: number; botNames?: string[] },
  ): Promise<void> {
    const job = this.jobs.get(jobId)!;

    // ── autoGather pre-stage ──
    // Only runs when caller asked for it. On timeout, fail the build before
    // any placement starts — there's no point dispatching a build that we
    // already know is missing materials.
    if (gatherOpts?.autoGather) {
      const gatherBots = gatherOpts.botNames && gatherOpts.botNames.length > 0
        ? gatherOpts.botNames
        : assignments.map((a) => a.botName);
      const requirement = this.computeMaterialRequirement(blocks);
      job.status = 'gathering';
      this.io.emit('build:gathering', { jobId });
      this.schedulePersist();
      const ready = await this.runAutoGather(
        jobId,
        gatherBots,
        requirement,
        gatherOpts.autoGatherTimeoutMs ?? AUTOGATHER_DEFAULT_TIMEOUT_MS,
      );
      if (!ready) {
        job.status = 'failed';
        this.io.emit('build:completed', { ...job, error: 'autoGather timeout' });
        this.eventLog.push({
          type: 'build:gather-timeout',
          botName: gatherBots.join(', '),
          description: `Build failed: autoGather pre-stage timed out`,
          metadata: { jobId },
        });
        this.schedulePersist();
        return;
      }
      job.status = 'placing';
      this.io.emit('build:placing', { jobId });
      this.schedulePersist();
    }

    // Pre-partition blocks by bot in a single pass so each parallel worker
    // doesn't re-filter the full block array.
    const blocksByBot = new Map<string, BlockEntry[]>();
    for (const a of assignments) blocksByBot.set(a.botName, []);
    for (const b of blocks) {
      for (const a of assignments) {
        if (b.localY >= a.yMin && b.localY <= a.yMax) {
          blocksByBot.get(a.botName)!.push(b);
          break;
        }
      }
    }

    // Execute assignments in retry ROUNDS. A transient server kick fails one
    // bot's assignment; rather than failing the whole job we wait a grace
    // period for the bot to reconnect and resume from blocksPlaced, retrying up
    // to MAX_BUILD_ROUNDS. This lets a build grind to completion through the
    // server's connection-throttle churn instead of dying on the first kick.
    const MAX_BUILD_ROUNDS = 8;
    const RECONNECT_GRACE_MS = 45000;
    for (let round = 0; round < MAX_BUILD_ROUNDS; round++) {
      if (this.cancelledJobs.has(jobId)) break;
      // Run every not-yet-completed assignment in parallel this round.
      const promises = assignments.map(async (assignment, i) => {
      // Skip assignments already finished in a prior round.
      if (assignment.status === 'completed') return;
      // Check for cancellation
      if (this.cancelledJobs.has(jobId)) return;

      // Get the worker handle (worker thread is the source of truth for the mineflayer bot)
      const handle = this.botManager.getWorker(assignment.botName) as any;
      const connected = handle ? await handle.isBotConnected() : false;
      if (!handle || !connected) {
        assignment.status = 'failed';
        logger.error({ jobId, bot: assignment.botName }, 'Bot not available for building');
        return;
      }

      // Pause voyager loop so the bot doesn't wander off
      handle.pauseVoyager('building');

      // Stop any current pathfinding/movement
      handle.stopMovement();

      // Set creative mode so bot can't die during build, then teleport to site
      try {
        const opHandles = (this.botManager.getAllWorkers() as any[])
          .filter((h: any) => h && h.botName !== assignment.botName);
        let opHandle: any = null;
        for (const oh of opHandles) {
          if (typeof oh.isBotConnected === 'function' && (await oh.isBotConnected())) {
            opHandle = oh;
            break;
          }
        }
        const cmds = [
          `/gamemode creative ${assignment.botName}`,
          `/tp ${assignment.botName} ${job.origin.x} ${job.origin.y + 50} ${job.origin.z}`,
        ];
        for (const cmd of cmds) {
          if (opHandle) opHandle.chat(cmd);
          handle.chat(cmd);
          await this.sleep(500);
        }
        await this.sleep(1000);
        logger.info({ bot: assignment.botName }, 'Set creative mode and teleported to build site');
      } catch (e) {
        logger.warn({ bot: assignment.botName }, 'Failed to prepare bot for building');
      }

      // Set bot state to BUILDING
      handle.setBotState(BotState.BUILDING);
      assignment.status = 'building';

      this.io.emit('build:bot-status', {
        jobId,
        botName: assignment.botName,
        status: 'building',
        yMin: assignment.yMin,
        yMax: assignment.yMax,
      });

      // Pre-partitioned slice for this bot.
      const botBlocks = blocksByBot.get(assignment.botName) ?? [];

      // Stagger start by 2s per bot to avoid overwhelming server
      if (i > 0) await new Promise((r) => setTimeout(r, i * 2000));

      try {
        await this.executeBotAssignment(jobId, job, assignment, handle, botBlocks);
        assignment.status = 'completed';
      } catch (err: any) {
        assignment.status = 'failed';
        logger.error({ jobId, bot: assignment.botName, err: err.message }, 'Bot assignment failed');
      }

      // Switch back to survival and reset bot state
      try {
        handle.chat(`/gamemode survival ${assignment.botName}`);
      } catch {}
      handle.setBotState(BotState.IDLE);
      handle.resumeVoyager();

      this.io.emit('build:bot-status', {
        jobId,
        botName: assignment.botName,
        status: assignment.status,
        blocksPlaced: assignment.blocksPlaced,
      });
    });

    await Promise.all(promises);

      // Round complete. Stop if cancelled, every assignment finished, or every
      // block is in the world; otherwise wait for kicked bots to reconnect and
      // run another round (each bot resumes from its blocksPlaced).
      if (this.cancelledJobs.has(jobId)) break;
      if (assignments.every((a) => a.status === 'completed')) break;
      if (job.placedBlocks >= job.totalBlocks) break;
      if (round < MAX_BUILD_ROUNDS - 1) {
        const incomplete = assignments
          .filter((a) => a.status !== 'completed')
          .map((a) => a.botName);
        logger.warn(
          { jobId, round: round + 1, incomplete, placed: job.placedBlocks, total: job.totalBlocks },
          'Build round incomplete — waiting for reconnects, then retrying',
        );
        await this.sleep(RECONNECT_GRACE_MS);
      }
    }

    // ── Post-placement bunker entry build (underground mode only) ──
    // Runs ONCE per job after every bot has finished its slice. Uses any
    // still-connected bot as the op'd probe; failures here are non-fatal —
    // the bunker itself is built, just the stairwell may be missing.
    const bunker = this.bunkerContext.get(jobId);
    if (bunker && !this.cancelledJobs.has(jobId)) {
      const anyPlaced = job.placedBlocks > 0;
      if (anyPlaced) {
        let entryHandle: any = this.botManager.getWorker(bunker.probeBotName);
        if (!entryHandle || !(typeof entryHandle.isBotConnected === 'function' && await entryHandle.isBotConnected())) {
          // Fall back to any other connected bot.
          entryHandle = null;
          for (const a of assignments) {
            const h = this.botManager.getWorker(a.botName) as any;
            if (h && typeof h.isBotConnected === 'function' && (await h.isBotConnected())) {
              entryHandle = h; break;
            }
          }
        }
        if (entryHandle) {
          try {
            const entry = await runBunkerEntry(entryHandle, bunker.origin, bunker.schSize, bunker.surfaceY);
            logger.info(
              { jobId, placed: entry.blocksPlaced, torches: entry.torchesPlaced, warnings: entry.warnings.length },
              'Bunker entry stairwell built',
            );
          } catch (err: any) {
            logger.warn({ jobId, err: err.message }, 'Bunker entry build failed (bunker itself is intact)');
          }
        } else {
          logger.warn({ jobId }, 'Bunker entry skipped — no connected bot available');
        }
      }
      this.bunkerContext.delete(jobId);
    }

    // ── Real verification + repair sweep ──
    // The placement loop only spot-checks 1-in-20 blocks (fire-and-forget), so
    // silent /setblock drops and chunk-load gaps were counted as "placed" —
    // producing hollow builds that reported as complete. This sweep reads every
    // target block back via an op bot (teleporting it across the volume to force
    // chunk loads) and re-places any mismatch, giving the true residual hole
    // count that the job status below is based on.
    let sweepRan = false;
    let sweepMissing = 0;
    if (!this.cancelledJobs.has(jobId) && job.placedBlocks > 0) {
      try {
        const sweep = await this.verifyAndRepairJob(jobId, job, blocks, assignments);
        if (sweep.checked > 0) {
          sweepRan = true;
          sweepMissing = sweep.missing;
        }
        logger.info(
          { jobId, checked: sweep.checked, repaired: sweep.repaired, missing: sweep.missing,
            preserved: sweep.preserved, sample: sweep.sampleMisses.slice(0, 8) },
          'Build verify-and-repair sweep complete',
        );
      } catch (err: any) {
        logger.warn({ jobId, err: err.message }, 'Verify-and-repair sweep failed — falling back to inline counters');
      }
    }

    // Final status
    if (!this.cancelledJobs.has(jobId)) {
      const allCompleted = assignments.every((a) => a.status === 'completed');
      // The sweep is authoritative for holes actually present in the world. The
      // inline per-bot counter is only a fast-path hint (and undercounts because
      // it spot-checks 1-in-20). Prefer the sweep's residual miss count; fall
      // back to inline counters only if the sweep couldn't run (no op bot).
      const inlineFailed = assignments.reduce((sum, a) => sum + (a.failedBlocks ?? 0), 0);
      const failedBlockCount = sweepRan ? sweepMissing : inlineFailed;
      job.failedBlockCount = failedBlockCount;
      // A job is complete only if every assignment finished OR every block was
      // sent — AND the verify sweep found zero residual holes. A non-zero
      // residual means real gaps remain, so we report completed_with_errors
      // (loud) rather than a silent 'completed'.
      const placedAll = job.placedBlocks >= job.totalBlocks;
      if (allCompleted || placedAll) {
        job.status = failedBlockCount > 0 ? 'completed_with_errors' : 'completed';
      } else {
        job.status = 'failed';
      }

      const hooks = this.jobHooks.get(jobId);
      if (hooks?.onCompleted) {
        try { hooks.onCompleted(job); } catch (err: any) {
          logger.warn({ err: err.message, jobId }, 'onCompleted hook threw');
        }
      }
      this.jobHooks.delete(jobId);

      this.io.emit('build:completed', job);
      this.eventLog.push({
        type: 'build:completed',
        botName: assignments.map((a) => a.botName).join(', '),
        description: `Build ${job.status}: ${job.schematicFile} (${job.placedBlocks}/${job.totalBlocks} blocks, ${failedBlockCount} missing after verify)`,
        metadata: { jobId, status: job.status, failedBlockCount },
      });

      logger.info(
        { jobId, status: job.status, placed: job.placedBlocks, total: job.totalBlocks, failedBlockCount },
        'Build finished',
      );
      this.persistJobs();

      // Remove bots that were created specifically for this build
      if (job.cleanupBotNames && job.cleanupBotNames.length > 0) {
        for (const botName of job.cleanupBotNames) {
          try {
            const removed = await this.botManager.removeBot(botName);
            if (removed) {
              logger.info({ botName, jobId }, 'Cleaned up build bot');
              this.io.emit('bot:disconnect', { bot: botName });
            }
          } catch (err) {
            logger.warn({ botName, jobId, err }, 'Failed to cleanup build bot');
          }
        }
      }
    }

    // Cleanup cancellation & reassignment tracking
    this.cancelledJobs.delete(jobId);
    this.reassignQueues.delete(jobId);

    // Schedule a delayed eviction of the terminal job + companion maps so the
    // dashboard can still show "just finished" reads, but the job objects
    // (which carry the full assignments/placedBlocks arrays — up to ~500KB
    // each for a town hall) don't pin memory forever. 1h grace covers any
    // realistic dashboard refresh window. Resumed jobs aren't affected: the
    // hook runs only after a job reaches a terminal status.
    this.scheduleTerminalJobEviction(jobId);
  }

  /** Drop a terminal job + all companion-map entries after the grace window. */
  private scheduleTerminalJobEviction(jobId: string, graceMs: number = 60 * 60 * 1000): void {
    const t = setTimeout(() => {
      this.jobs.delete(jobId);
      this.jobOptions.delete(jobId);
      this.bunkerContext.delete(jobId);
      this.gatherJobs.delete(jobId);
      this.cancelledJobs.delete(jobId);
      this.reassignQueues.delete(jobId);
      this.persistJobs();
      logger.debug({ jobId }, 'BuildCoordinator: evicted terminal job from in-memory maps');
    }, graceMs);
    t.unref?.();
  }

  /**
   * Return the first connected (and therefore op'd — all build bots are op'd
   * server-side) worker handle, preferring the names in `preferred`. Used to
   * pick a verifier/repairer for the post-build sweep that can survive one of
   * the build bots having dropped.
   */
  private async pickConnectedBot(preferred: string[] = []): Promise<any> {
    const order = [
      ...preferred,
      ...((this.botManager.getAllWorkers() as any[]).map((h) => h?.botName).filter(Boolean) as string[]),
    ];
    const seen = new Set<string>();
    for (const name of order) {
      if (!name || seen.has(name)) continue;
      seen.add(name);
      const h = this.botManager.getWorker(name) as any;
      if (h && typeof h.isBotConnected === 'function') {
        try { if (await h.isBotConnected()) return h; } catch { /* try next */ }
      }
    }
    return null;
  }

  /**
   * Verify-and-repair sweep, the real fix for hollow builds reported as
   * complete. Reads every (non-air) target block back from the world via an op
   * bot and re-issues /setblock for any that don't match, repeating up to
   * MAX_ROUNDS over the shrinking miss set.
   *
   * getBlockAt only returns a real block for chunks the bot's client has
   * loaded, so we bucket targets by a (REGION × REGION) XZ grid and teleport
   * the verifier to each bucket's centre (above the build) before reading it —
   * this forces the chunks to load and makes the read trustworthy. Placement
   * itself (op /setblock) lands server-side regardless of where the bot stands,
   * so re-placement does not need a teleport.
   *
   * Returns the number of targets checked, how many were repaired, and the
   * residual miss count (true holes) plus a sample of their coords.
   */
  /** AIR-equivalent block names the verify sweep treats as "empty". */
  private static readonly VERIFY_AIR_NAMES = new Set(['air', 'cave_air', 'void_air']);

  /**
   * Classify a verify read (holes-only repair, review #5d):
   *   'ok'       — current block matches the target, nothing to do.
   *   'preserve' — a DIFFERENT non-air block is here (almost always a player
   *                edit / legit terrain). Leave it; do NOT /setblock replace.
   *   'repair'   — air or unreadable-empty: a genuine dropped placement to
   *                re-place.
   * `got` is the normalized (minecraft:-stripped) world block name, or null.
   */
  private classifyVerifyRead(got: string | null, target: string): 'ok' | 'preserve' | 'repair' {
    if (got === target) return 'ok';
    if (got && !BuildCoordinator.VERIFY_AIR_NAMES.has(got)) return 'preserve';
    return 'repair';
  }

  private async verifyAndRepairJob(
    jobId: string,
    job: BuildJob,
    blocks: BlockEntry[],
    assignments: BotAssignment[],
  ): Promise<{
    checked: number;
    repaired: number;
    missing: number;
    preserved: number;
    sampleMisses: Array<{ x: number; y: number; z: number; want: string; got: string }>;
  }> {
    // loadSchematicCached already excludes air, so every entry is a solid target.
    return this.verifyAndRepairTargets(jobId, blocks, assignments.map((a) => a.botName));
  }

  /**
   * Reusable core of the verify-and-repair sweep (see verifyAndRepairJob).
   * `label` is used for cancellation lookup (a real jobId) and logging; pass
   * null for ad-hoc structures like the tunnel that aren't tracked as jobs.
   */
  private async verifyAndRepairTargets(
    label: string | null,
    targets: BlockEntry[],
    preferredBots: string[],
  ): Promise<{
    checked: number;
    repaired: number;
    missing: number;
    preserved: number;
    sampleMisses: Array<{ x: number; y: number; z: number; want: string; got: string }>;
  }> {
    const isCancelled = () => label !== null && this.cancelledJobs.has(label);
    if (targets.length === 0) return { checked: 0, repaired: 0, missing: 0, preserved: 0, sampleMisses: [] };

    let verifier = await this.pickConnectedBot(preferredBots);
    if (!verifier) {
      logger.warn({ label }, 'Verify sweep skipped — no connected bot available to read the world back');
      return { checked: 0, repaired: 0, missing: 0, preserved: 0, sampleMisses: [] };
    }

    const norm = (n: string | undefined): string | null =>
      n ? (n.startsWith('minecraft:') ? n.slice('minecraft:'.length) : n) : null;
    const REGION = 32;       // XZ bucket size; comfortably inside render distance
    const READ_CONCURRENCY = 16;
    const PLACE_PACE_MS = 55; // gentle on the chat-spam limit during repairs
    const MAX_ROUNDS = 3;
    const bucketKey = (b: BlockEntry) => `${Math.floor(b.wx / REGION)},${Math.floor(b.wz / REGION)}`;

    // Pause the verifier where it is and drop it into creative so it survives
    // wherever we teleport it. Restored in the finally block.
    const botName = verifier.botName;
    try { verifier.pauseVoyager?.('verifying build'); } catch { /* best effort */ }
    try { verifier.stopMovement?.(); } catch { /* best effort */ }
    try { verifier.chat(`/gamemode creative ${botName}`); } catch { /* best effort */ }

    // After a teleport the server streams the new chunks asynchronously and
    // bot.blockAt returns null until they arrive — a fixed sleep is racy and was
    // counting still-loading blocks as "missing". Poll a sentinel coordinate in
    // the region (any non-null result, including air, means the chunk loaded)
    // before trusting reads. Returns false if it never loaded within the budget.
    const waitChunkLoaded = async (x: number, y: number, z: number): Promise<boolean> => {
      for (let t = 0; t < 15; t++) { // up to ~6s
        try { if (await verifier.getBlockAt(x, y, z)) return true; } catch { /* retry */ }
        await this.sleep(400);
      }
      return false;
    };

    let toCheck = targets;
    let missing: BlockEntry[] = [];
    let initialMissing = -1; // miss count on the first read, before any repair
    let preservedTotal = 0;  // non-air mismatches left intact (likely player edits)
    const sampleMisses: Array<{ x: number; y: number; z: number; want: string; got: string }> = [];

    try {
      for (let round = 0; round < MAX_ROUNDS; round++) {
        if (isCancelled()) break;

        // Re-acquire a connected verifier if the current one dropped.
        if (!(typeof verifier.isBotConnected === 'function' && (await verifier.isBotConnected()))) {
          const next = await this.pickConnectedBot(preferredBots);
          if (!next) {
            logger.warn({ label, round }, 'Verify sweep aborted mid-way — lost all connected bots');
            break;
          }
          verifier = next;
          try { verifier.chat(`/gamemode creative ${verifier.botName}`); } catch { /* best effort */ }
        }

        // Bucket the current miss set by XZ region.
        const buckets = new Map<string, BlockEntry[]>();
        for (const b of toCheck) {
          const k = bucketKey(b);
          let arr = buckets.get(k);
          if (!arr) { arr = []; buckets.set(k, arr); }
          arr.push(b);
        }

        const roundMisses: BlockEntry[] = [];   // read as air/empty → re-place + re-check
        const unverified: BlockEntry[] = [];     // chunk never loaded → re-check only
        const preservedMods: BlockEntry[] = [];  // non-air mismatch → left intact (player edit)
        for (const bucket of buckets.values()) {
          if (isCancelled()) break;

          // Teleport the verifier above this bucket, then WAIT for the chunk to
          // actually arrive (not a fixed sleep) so reads are trustworthy.
          const cx = Math.round(bucket.reduce((s, b) => s + b.wx, 0) / bucket.length);
          const cz = Math.round(bucket.reduce((s, b) => s + b.wz, 0) / bucket.length);
          const topY = Math.max(...bucket.map((b) => b.wy));
          try { verifier.chat(`/tp ${verifier.botName} ${cx} ${topY + 25} ${cz}`); } catch { /* best effort */ }
          await this.sleep(300);
          const loaded = await waitChunkLoaded(cx, topY, cz);
          if (!loaded) {
            logger.warn(
              { label, round: round + 1, at: { x: cx, z: cz }, bucketSize: bucket.length },
              'Verify sweep: chunk did not load after teleport — re-checking this region next round',
            );
            // Don't declare these missing (no pointless re-placement); just
            // re-check them next round once the chunk hopefully loads.
            for (const b of bucket) unverified.push(b);
            continue;
          }

          // Read the whole bucket back in parallel batches.
          for (let i = 0; i < bucket.length; i += READ_CONCURRENCY) {
            const slice = bucket.slice(i, i + READ_CONCURRENCY);
            const reads = await Promise.all(slice.map(async (b) => {
              try { return await verifier.getBlockAt(b.wx, b.wy, b.wz); }
              catch { return null; }
            }));
            for (let j = 0; j < slice.length; j++) {
              const b = slice[j];
              const got = norm(reads[j]?.name);
              const verdict = this.classifyVerifyRead(got, b.name);
              if (verdict === 'ok') continue;
              if (verdict === 'preserve') {
                // A different non-air block sits here — almost always a player
                // edit (or legit terrain variation), not a dropped placement.
                // Overwriting it with /setblock replace would silently revert
                // player builds on every completion + re-run. Preserve it.
                preservedMods.push(b);
                continue;
              }
              // air / unreadable-empty → genuine dropped placement → re-place.
              roundMisses.push(b);
            }
          }
        }

        // Re-place only the confirmed misses (server-side op /setblock — no tp
        // needed). Unverified (chunk-not-loaded) blocks are left alone.
        for (const b of roundMisses) {
          if (isCancelled()) break;
          const spec = b.stateStr ? `${b.name}[${b.stateStr}]` : b.name;
          try { verifier.chat(`/setblock ${b.wx} ${b.wy} ${b.wz} minecraft:${spec} replace`); } catch { /* best effort */ }
          await this.sleep(PLACE_PACE_MS);
        }

        preservedTotal += preservedMods.length;
        const residual = roundMisses.concat(unverified);
        missing = residual;
        if (initialMissing < 0) initialMissing = residual.length;
        logger.info(
          { label, round: round + 1, checkedThisRound: toCheck.length,
            confirmedMissing: roundMisses.length, unverified: unverified.length,
            preserved: preservedMods.length },
          'Verify sweep round complete',
        );
        if (residual.length === 0) break;
        toCheck = residual;       // next round re-checks misses + unverified
        await this.sleep(600);
      }
    } finally {
      try { verifier.chat(`/gamemode survival ${verifier.botName}`); } catch { /* best effort */ }
      try { verifier.setBotState?.(BotState.IDLE); } catch { /* best effort */ }
      try { verifier.resumeVoyager?.(); } catch { /* best effort */ }
    }

    for (const b of missing.slice(0, 12)) {
      sampleMisses.push({ x: b.wx, y: b.wy, z: b.wz, want: b.name, got: 'absent/mismatch' });
    }
    return {
      checked: targets.length,
      repaired: initialMissing < 0 ? 0 : Math.max(0, initialMissing - missing.length),
      missing: missing.length,
      preserved: preservedTotal,
      sampleMisses,
    };
  }

  private async executeBotAssignment(
    jobId: string,
    job: BuildJob,
    assignment: BotAssignment,
    handle: any,
    blocks: BlockEntry[],
  ): Promise<void> {
    // Resume: skip blocks already placed in a prior run.
    const startIdx = Math.min(assignment.blocksPlaced, blocks.length);
    if (startIdx > 0) {
      logger.info(
        { jobId, bot: assignment.botName, skipping: startIdx, remaining: blocks.length - startIdx },
        'Resuming bot assignment — skipping already-placed blocks',
      );
    }

    // Per-block placement cadence. The old 250ms sleep + an IPC verify probe on
    // EVERY block made large builds crawl (~4s/block in practice → ~18h for a
    // 15k schematic). We now place with a short sleep and only verify
    // periodically (see PLACE loop) — /setblock is reliable on an op'd bot, so
    // spot-checking is enough and keeps the chat cadence under the spam limit.
    const PLACE_SLEEP_MS = 60;
    // Place a single block. verify=false is fire-and-forget (fast path);
    // verify=true probes + retries to catch genuine failures.
    const placeOne = async (block: BlockEntry, verify: boolean): Promise<void> => {
      const blockSpec = block.stateStr ? `${block.name}[${block.stateStr}]` : block.name;
      if (!verify) {
        handle.chat(`/setblock ${block.wx} ${block.wy} ${block.wz} minecraft:${blockSpec} replace`);
        await this.sleep(PLACE_SLEEP_MS);
        return;
      }
      const MAX_ATTEMPTS = 3;
      for (let attempt = 1; attempt <= MAX_ATTEMPTS; attempt++) {
        handle.chat(`/setblock ${block.wx} ${block.wy} ${block.wz} minecraft:${blockSpec} replace`);
        await this.sleep(PLACE_SLEEP_MS);

        // Verify placement via IPC probe on the bot's own handle.
        let placed: { name: string } | null = null;
        try {
          placed = typeof handle.getBlockAt === 'function'
            ? await handle.getBlockAt(block.wx, block.wy, block.wz)
            : null;
        } catch {
          placed = null;
        }

        // Probe returned nothing (chunk not loaded / transient IPC error). On
        // an early attempt, re-issue /setblock next loop so the block isn't
        // silently dropped if the chunk just hadn't loaded yet (the cause of
        // hollow builds). On the final attempt accept optimistically — the
        // /setblock likely landed server-side and re-issuing forever risks a
        // chat-spam kick.
        if (!placed) {
          if (attempt < MAX_ATTEMPTS) continue;
          return;
        }

        const placedName = placed.name?.startsWith('minecraft:')
          ? placed.name.slice('minecraft:'.length)
          : placed.name;
        if (placedName === block.name) return;

        if (attempt < MAX_ATTEMPTS) {
          logger.warn(
            { jobId, bot: assignment.botName, at: { x: block.wx, y: block.wy, z: block.wz }, want: block.name, got: placedName, attempt },
            'Block verify failed — retrying /setblock',
          );
        } else {
          assignment.failedBlocks++;
          logger.warn(
            { jobId, bot: assignment.botName, at: { x: block.wx, y: block.wy, z: block.wz }, want: block.name, got: placedName },
            'Block verify failed after max retries — counting as failed',
          );
        }
      }
    };

    let bi = startIdx;
    while (bi < blocks.length) {
      const block = blocks[bi];
      // Check cancellation
      if (this.cancelledJobs.has(jobId)) return;

      // Check pause — spin-wait with sleep
      while (this.pausedJobs.has(jobId)) {
        if (this.cancelledJobs.has(jobId)) return;
        await this.sleep(500);
      }

      // Bot-death check only every 25 blocks — each check is an IPC round-trip,
      // and doing it per block was a major slowdown. Between checks we assume
      // the bot is still connected; a death is caught within 25 blocks.
      if (bi % 25 === 0) {
        const alive = typeof handle.isAlive === 'function' ? handle.isAlive() : true;
        let connected = true;
        if (alive && typeof handle.isBotConnected === 'function') {
          try {
            connected = await handle.isBotConnected();
          } catch {
            connected = false;
          }
        }
        if (!alive || !connected) {
          const remaining = blocks.slice(bi);
          assignment.status = 'failed';
          logger.error(
            { jobId, bot: assignment.botName, remaining: remaining.length, placed: assignment.blocksPlaced },
            'Bot disconnected mid-build — attempting reassignment',
          );
          this.reassignRemaining(jobId, job, assignment, remaining);
          throw new Error('bot disconnected');
        }
      }

      // Spot-verify every 20th block; the rest are fast fire-and-forget.
      await placeOne(block, bi % 20 === 0);

      assignment.blocksPlaced++;
      assignment.currentY = block.localY;
      job.placedBlocks++;

      // Emit progress every 20 blocks
      if (job.placedBlocks % 20 === 0) {
        const pct = Math.round((job.placedBlocks / job.totalBlocks) * 100);
        this.io.emit('build:progress', {
          jobId,
          placedBlocks: job.placedBlocks,
          totalBlocks: job.totalBlocks,
          percentage: pct,
          botName: assignment.botName,
          botBlocksPlaced: assignment.blocksPlaced,
          botBlocksTotal: assignment.blocksTotal,
        });
        // Fire onProgress hook at 25/50/75% milestones only — keeps chat
        // announcements from spamming every 20 blocks.
        const hooks = this.jobHooks.get(jobId);
        if (hooks?.onProgress) {
          const milestone = pct >= 75 ? 75 : pct >= 50 ? 50 : pct >= 25 ? 25 : 0;
          if (milestone > hooks.lastMilestone) {
            hooks.lastMilestone = milestone;
            try {
              hooks.onProgress({ jobId, placed: job.placedBlocks, total: job.totalBlocks, pct: milestone });
            } catch (err: any) {
              logger.warn({ err: err.message, jobId }, 'onProgress hook threw');
            }
          }
        }
        // Throttled persist so a crash loses at most ~2s of progress
        this.schedulePersist();
      }
      bi++;

      // Between placements, absorb any reassigned blocks handed to this bot.
      if (bi >= blocks.length) {
        const extras = this.drainReassignedBlocks(jobId, assignment.botName);
        if (extras.length > 0) {
          logger.info(
            { jobId, bot: assignment.botName, absorbing: extras.length },
            'Absorbing reassigned blocks from a dead bot',
          );
          blocks.push(...extras);
          assignment.blocksTotal += extras.length;
        }
      }
    }
  }

  /**
   * Try to hand off the `remaining` blocks of a dead bot's assignment to
   * another bot in the same job whose worker is still alive + connected.
   * Prefers a bot that has already completed its own slice (spec-preferred),
   * but falls back to any still-alive bot so blocks are not lost when a
   * death happens mid-build. Each recipient drains the reassign queue after
   * finishing its own blocks. Single-pass — no full re-balancing.
   */
  private reassignRemaining(
    jobId: string,
    job: BuildJob,
    deadAssignment: BotAssignment,
    remaining: BlockEntry[],
  ): void {
    if (remaining.length === 0) return;

    const candidates = job.assignments.filter((a) => {
      if (a === deadAssignment) return false;
      if (a.status === 'failed') return false;
      const h = this.botManager.getWorker(a.botName) as any;
      if (!h) return false;
      if (typeof h.isAlive === 'function' && !h.isAlive()) return false;
      return true;
    });
    // Prefer completed (now-idle) bots over still-building ones.
    candidates.sort((a, b) => {
      const rank = (s: string) => (s === 'completed' ? 0 : s === 'building' ? 1 : 2);
      return rank(a.status) - rank(b.status);
    });
    const target = candidates[0];
    if (!target) {
      logger.warn(
        { jobId, from: deadAssignment.botName, blocks: remaining.length },
        'No spare bot available — dead bot\'s remaining blocks will be lost',
      );
      return;
    }

    let jobQueues = this.reassignQueues.get(jobId);
    if (!jobQueues) {
      jobQueues = new Map();
      this.reassignQueues.set(jobId, jobQueues);
    }
    const existing = jobQueues.get(target.botName) ?? [];
    existing.push(...remaining);
    jobQueues.set(target.botName, existing);

    this.io.emit('build:reassign', {
      jobId,
      from: deadAssignment.botName,
      to: target.botName,
      blocks: remaining.length,
    });
    logger.info(
      { jobId, from: deadAssignment.botName, to: target.botName, blocks: remaining.length, targetStatus: target.status },
      'Reassigning remaining blocks to alive bot',
    );
  }

  /** Drain and return any pending reassigned blocks for a given bot. */
  private drainReassignedBlocks(jobId: string, botName: string): BlockEntry[] {
    const jobQueues = this.reassignQueues.get(jobId);
    if (!jobQueues) return [];
    const queue = jobQueues.get(botName);
    if (!queue || queue.length === 0) return [];
    jobQueues.set(botName, []);
    return queue;
  }

  /**
   * Map an `originMode` string to a concrete {x, y, z}. The caller-supplied
   * `fallbackOrigin` is used as-is for 'coords' mode and as a hint for the
   * Y-coordinate when resolving a bot or player position that has no Y info.
   */
  private async resolveOrigin(
    originMode: string,
    fallbackOrigin: { x: number; y: number; z: number },
    botNames: string[],
    probeHandle: any,
    schSize: { x: number; y: number; z: number },
  ): Promise<{ x: number; y: number; z: number }> {
    if (originMode === 'coords') return fallbackOrigin;

    if (originMode === 'auto-flat') {
      if (!probeHandle) {
        logger.warn('originMode auto-flat: no probe bot available, falling back to supplied origin');
        return fallbackOrigin;
      }
      // Seed the SiteSelector spiral at the CALLER's supplied origin (e.g. the
      // town capital), not the probe bot's current position. Otherwise residents
      // who wander far from the capital drag the search spiral with them, and
      // SiteSelector can't find flat ground anywhere near the town centre.
      // The probe is only used here as an IPC handle for reading blocks.
      const probe = (x: number, y: number, z: number) => probeHandle.getBlockAt(x, y, z);
      const cand: SiteCandidate | null = await selectBuildSite(probe, fallbackOrigin, schSize);
      if (!cand) {
        logger.warn({ refPos: fallbackOrigin }, 'originMode auto-flat: no acceptable site found near supplied origin, using supplied origin');
        return fallbackOrigin;
      }
      logger.info({ origin: cand.origin, confidence: cand.confidence, reasons: cand.reasons }, 'Build: auto-flat site selected');
      return cand.origin;
    }

    if (originMode.startsWith('bot:')) {
      const botName = originMode.slice(4);
      const handle = this.botManager.getWorker(botName) as any;
      if (!handle) {
        logger.warn({ botName }, 'originMode bot:<name>: worker not found, falling back to supplied origin');
        return fallbackOrigin;
      }
      const status = handle.getCachedStatus?.();
      const pos = status?.position;
      if (!pos) {
        logger.warn({ botName }, 'originMode bot:<name>: no cached position, falling back');
        return fallbackOrigin;
      }
      logger.info({ botName, pos }, 'Build: anchored to bot position');
      return { x: Math.floor(pos.x), y: Math.floor(pos.y), z: Math.floor(pos.z) };
    }

    if (originMode.startsWith('player:')) {
      const playerName = originMode.slice(7);
      // Try probe.getPlayers — returns current world positions for online players.
      if (probeHandle?.getPlayers) {
        try {
          const players = await probeHandle.getPlayers();
          const hit = players.find((p: any) => p.name?.toLowerCase() === playerName.toLowerCase());
          if (hit?.position) {
            logger.info({ playerName, pos: hit.position }, 'Build: anchored to player position');
            return { x: Math.floor(hit.position.x), y: Math.floor(hit.position.y), z: Math.floor(hit.position.z) };
          }
        } catch (err: any) {
          logger.warn({ playerName, err: err.message }, 'originMode player:<name>: getPlayers failed');
        }
      }
      logger.warn({ playerName }, 'originMode player:<name>: player not visible to probe, falling back to supplied origin');
      return fallbackOrigin;
    }

    logger.warn({ originMode }, 'Unknown originMode, using supplied origin');
    return fallbackOrigin;
  }

  /**
   * Clear the world volume enclosing the schematic footprint by issuing
   * `/fill ... air destroy` commands from the probe bot. Commands are issued
   * one Y-slab at a time so we never exceed Minecraft's 32768-block fill cap.
   */
  private async runClearSite(
    probeHandle: any,
    opts: {
      footprintMin: { x: number; y: number; z: number };
      footprintMax: { x: number; y: number; z: number };
      clearanceHeight?: number;
    },
  ): Promise<{ cleared: number; errors: string[]; skipped: number }> {
    const errors: string[] = [];
    const { footprintMin, footprintMax } = opts;
    const x1 = Math.min(footprintMin.x, footprintMax.x);
    const x2 = Math.max(footprintMin.x, footprintMax.x);
    const z1 = Math.min(footprintMin.z, footprintMax.z);
    const z2 = Math.max(footprintMin.z, footprintMax.z);
    const yBase = Math.min(footprintMin.y, footprintMax.y);
    const height = opts.clearanceHeight ?? (Math.abs(footprintMax.y - footprintMin.y) + 1);
    const yTop = yBase + height - 1;

    const area = (x2 - x1 + 1) * (z2 - z1 + 1);
    if (area <= 0) return { cleared: 0, errors, skipped: 0 };

    // Fill cap is 32768 blocks. Slab size per Y is `area` — slice Y to stay under cap.
    const slabThickness = Math.max(1, Math.floor(32768 / Math.max(1, area)));
    let slabs = 0;
    let skipped = 0;

    for (let ySlabStart = yBase; ySlabStart <= yTop; ySlabStart += slabThickness) {
      const ySlabEnd = Math.min(ySlabStart + slabThickness - 1, yTop);
      // Geofence guard. `/fill ... air destroy` is an op command and bypasses
      // the per-block `bot.dig` geofence, so without this a clearSite (enabled
      // by default, and forced on for town builds) would wipe any protected
      // build that overlaps the footprint+clearance. Skip slabs that intersect
      // a protected zone rather than destroying it.
      const hit = intersectsProtectedZone(
        { x: x1, y: ySlabStart, z: z1 },
        { x: x2, y: ySlabEnd, z: z2 },
      );
      if (hit) {
        skipped++;
        logger.warn(
          { zone: hit.name ?? 'unnamed', ySlabStart, ySlabEnd, x1, x2, z1, z2 },
          'Site-prep clearSite: slab intersects a protected zone — skipping /fill air destroy',
        );
        continue;
      }
      const cmd = `/fill ${x1} ${ySlabStart} ${z1} ${x2} ${ySlabEnd} ${z2} air destroy`;
      try {
        probeHandle.chat(cmd);
        slabs++;
      } catch (err: any) {
        errors.push(err?.message ?? String(err));
      }
      // Small delay between fill commands to avoid overrunning the server tick.
      await this.sleep(200);
    }

    return { cleared: slabs, errors, skipped };
  }

  // ── autoGather pre-stage ────────────────────────────────

  /**
   * Strip a `minecraft:` namespace prefix and normalize to lowercase. Used to
   * align schematic palette keys (`minecraft:cobblestone`) with inventory and
   * skill resource names (`cobblestone`).
   */
  private normalizeResource(name: string): string {
    if (!name) return '';
    const n = name.toLowerCase();
    return n.startsWith('minecraft:') ? n.slice('minecraft:'.length) : n;
  }

  /**
   * Discover gather-skill chunk sizes by reading `skills/` filenames.
   *
   * Each filename matching `(mine|craft)_<N>_<resource>(_suffix)?.js` is
   * parsed; resources are normalized by stripping common suffix words and
   * collapsed-token variants (e.g. `oakplanks` → `oak_planks`). We retain
   * the LARGEST chunk size per resource so a 32-block shortfall produces 4
   * tasks of 8 rather than 32 tasks of 1.
   *
   * The catalog is cached in-memory; pass `force` to re-scan.
   */
  protected getSkillChunkCatalog(force = false): Map<string, SkillChunk> {
    if (this.skillChunkCatalog && !force) return this.skillChunkCatalog;

    const catalog = new Map<string, SkillChunk>();
    const skillsDir = path.join(process.cwd(), 'skills');
    let files: string[] = [];
    try {
      files = fs.readdirSync(skillsDir).filter((f) => f.endsWith('.js'));
    } catch (err: any) {
      logger.warn({ err: err.message, skillsDir }, 'autoGather: skills/ unreadable, catalog empty');
      this.skillChunkCatalog = catalog;
      return catalog;
    }

    // Filenames like `mine_8_cobblestone.js`, `craft_4_oak_planks_using.js`.
    // Capture verb, N, and the meaningful resource token(s).
    const re = /^(mine|craft)_(\d+)_([a-z0-9_]+?)(?:_(?:from|using|to|at|the|in|near|with|nearby|more|of|by)(?:_.*)?)?\.js$/;
    for (const file of files) {
      const m = re.exec(file);
      if (!m) continue;
      const verb = m[1];
      const n = parseInt(m[2], 10);
      if (!Number.isFinite(n) || n <= 0) continue;
      let resource = m[3];

      // Normalize collapsed-token variants by inserting underscores between
      // recognized "<wood>planks" / "<wood>log" / similar fused words.
      resource = resource.replace(/^(oak|spruce|birch|jungle|acacia|dark_oak|cherry|mangrove)(planks|log|logs|door|fence|stairs|slab)$/,
        (_full, wood, suffix) => `${wood}_${suffix}`);

      // Strip trailing `_blocks` (e.g. `mine_10_andesite_blocks` → `andesite`).
      resource = resource.replace(/_blocks?$/, '');

      // Drop leading filler tokens that aren't part of the resource name.
      // `blocks_of_stone` → `stone`; `more_cobblestone` → `cobblestone`.
      resource = resource.replace(/^(blocks?_of_|more_)/, '');

      // Add the bare resource plus a plural/singular variant so a schematic
      // key of `oak_log` aligns with a skill of `mine_3_oak_logs`.
      const variants = new Set<string>([resource]);
      if (resource.endsWith('s') && !resource.endsWith('ss')) {
        variants.add(resource.slice(0, -1));
      } else {
        variants.add(`${resource}s`);
      }

      for (const r of variants) {
        const existing = catalog.get(r);
        if (!existing || existing.chunkSize < n) {
          const verbWord = verb === 'mine' ? 'Mine' : 'Craft';
          catalog.set(r, {
            resource: r,
            chunkSize: n,
            skillFile: file,
            description: `${verbWord} ${n} ${resource}`,
          });
        }
      }
    }

    this.skillChunkCatalog = catalog;
    logger.info(
      { resources: catalog.size },
      'autoGather: skill chunk catalog built',
    );
    return catalog;
  }

  /**
   * Compute the per-material requirement for a schematic from its block list.
   * Returns a map keyed by NORMALIZED block name (no `minecraft:` prefix).
   */
  private computeMaterialRequirement(
    blocks: ReadonlyArray<{ name: string }>,
  ): Map<string, number> {
    const req = new Map<string, number>();
    for (const b of blocks) {
      const key = this.normalizeResource(b.name);
      if (!key) continue;
      req.set(key, (req.get(key) ?? 0) + 1);
    }
    return req;
  }

  /**
   * Read a bot's cached inventory and return a normalized name → count map.
   *
   * Inventory shape on `lastDetailedStatus.inventory` is an array of
   * `{name, count, slot}` (see BotInstance.getDetailedStatus). We also fall
   * back to a `lastStatus.inventory` Record<string, number> shape because
   * the spec hint described it that way and tests may stub either form.
   */
  protected getBotInventory(botName: string): Map<string, number> {
    const handle = this.botManager.getWorker(botName) as any;
    if (!handle) return new Map();
    const detailed = handle.getCachedDetailedStatus?.();
    const basic = handle.getCachedStatus?.();
    const out = new Map<string, number>();

    const inv = detailed?.inventory ?? basic?.inventory;
    if (Array.isArray(inv)) {
      for (const item of inv) {
        if (!item || typeof item.name !== 'string') continue;
        const key = this.normalizeResource(item.name);
        const count = typeof item.count === 'number' ? item.count : 0;
        out.set(key, (out.get(key) ?? 0) + count);
      }
    } else if (inv && typeof inv === 'object') {
      for (const [k, v] of Object.entries(inv as Record<string, unknown>)) {
        const key = this.normalizeResource(k);
        const count = typeof v === 'number' ? v : 0;
        out.set(key, (out.get(key) ?? 0) + count);
      }
    }

    return out;
  }

  /**
   * Build the chunk plan: for each (bot, material) with a positive shortfall,
   * find the largest available chunk skill and queue
   * `floor(shortage / chunk_size) + 1` chunks. Caps at AUTOGATHER_MAX_CHUNKS.
   *
   * Returns the per-bot share of the schematic requirement too, so the
   * readiness check (later) can compare each bot against its own target.
   */
  protected planGather(
    botNames: string[],
    requirement: Map<string, number>,
  ): { plan: GatherPlanEntry[]; perBotTarget: Map<string, Map<string, number>> } {
    const catalog = this.getSkillChunkCatalog();
    const plan: GatherPlanEntry[] = [];
    const perBotTarget = new Map<string, Map<string, number>>();
    if (botNames.length === 0) return { plan, perBotTarget };

    // Each bot is responsible for an even share of every material.
    const share = (total: number, n: number) => Math.ceil(total / Math.max(1, n));

    for (const bot of botNames) {
      perBotTarget.set(bot, new Map());
    }
    for (const [material, total] of requirement.entries()) {
      const per = share(total, botNames.length);
      for (const bot of botNames) {
        perBotTarget.get(bot)!.set(material, per);
      }
    }

    let queued = 0;
    for (const bot of botNames) {
      if (queued >= AUTOGATHER_MAX_CHUNKS) break;
      const inv = this.getBotInventory(bot);
      const targets = perBotTarget.get(bot)!;
      for (const [material, target] of targets.entries()) {
        if (queued >= AUTOGATHER_MAX_CHUNKS) break;
        const have = inv.get(material) ?? 0;
        const shortage = target - have;
        if (shortage <= 0) continue;
        // Match resource against the catalog, trying the bare key and a
        // common plural/singular variant.
        const chunk =
          catalog.get(material) ??
          (material.endsWith('s')
            ? catalog.get(material.slice(0, -1))
            : catalog.get(`${material}s`));
        if (!chunk) {
          logger.debug(
            { bot, material, shortage },
            'autoGather: no skill chunk found for material — skipping',
          );
          continue;
        }
        const chunks = Math.floor(shortage / chunk.chunkSize) + 1;
        for (let i = 0; i < chunks; i++) {
          if (queued >= AUTOGATHER_MAX_CHUNKS) break;
          plan.push({
            botName: bot,
            resource: chunk.resource,
            chunkSize: chunk.chunkSize,
            description: chunk.description,
            skillFile: chunk.skillFile,
          });
          queued++;
        }
      }
    }

    return { plan, perBotTarget };
  }

  /**
   * Queue each gather entry on the corresponding worker as a player-task
   * (same channel as `POST /api/bots/:name/task`). Errors are non-fatal —
   * the readiness loop is what ultimately decides if the gather succeeded.
   */
  private dispatchGatherPlan(plan: GatherPlanEntry[]): void {
    for (const entry of plan) {
      const handle = this.botManager.getWorker(entry.botName) as any;
      if (!handle || typeof handle.sendCommand !== 'function') continue;
      try {
        handle.sendCommand('queueTask', {
          description: entry.description,
          source: 'build:autoGather',
        });
      } catch (err: any) {
        logger.warn(
          { bot: entry.botName, description: entry.description, err: err.message },
          'autoGather: failed to queue gather task',
        );
      }
    }
  }

  /**
   * Run the autoGather pre-stage. Returns `true` when every bot has cleared
   * its share of the material requirement, `false` on timeout. Cancelled
   * jobs short-circuit early. Does NOT mutate job.status — caller decides
   * what to do with the outcome.
   */
  private async runAutoGather(
    jobId: string,
    botNames: string[],
    requirement: Map<string, number>,
    timeoutMs: number,
  ): Promise<boolean> {
    const { plan, perBotTarget } = this.planGather(botNames, requirement);
    const job = this.jobs.get(jobId);
    if (job) job.gatherPlan = plan;
    this.gatherJobs.set(jobId, { plan, timeoutMs });

    if (plan.length === 0) {
      logger.info({ jobId }, 'autoGather: no shortfalls detected — skipping gather phase');
      return true;
    }

    logger.info(
      {
        jobId,
        chunks: plan.length,
        bots: botNames.length,
        materials: [...new Set(plan.map((p) => p.resource))].slice(0, 10),
      },
      'autoGather: queueing pre-stage gather tasks',
    );
    this.io.emit('build:gather-started', { jobId, plan });
    this.eventLog.push({
      type: 'build:gather-started',
      botName: botNames.join(', '),
      description: `autoGather: ${plan.length} chunks queued across ${botNames.length} bot(s)`,
      metadata: { jobId, plan },
    });

    this.dispatchGatherPlan(plan);

    const start = Date.now();
    while (Date.now() - start < timeoutMs) {
      if (this.cancelledJobs.has(jobId)) {
        logger.info({ jobId }, 'autoGather: cancelled mid-gather');
        return false;
      }
      // Inventory check: every bot must meet OR exceed its per-bot target
      // for every material in the requirement.
      let allReady = true;
      for (const bot of botNames) {
        const inv = this.getBotInventory(bot);
        const targets = perBotTarget.get(bot)!;
        for (const [mat, want] of targets.entries()) {
          const have = inv.get(mat) ?? 0;
          if (have < want) { allReady = false; break; }
        }
        if (!allReady) break;
      }
      if (allReady) {
        logger.info(
          { jobId, elapsedMs: Date.now() - start },
          'autoGather: all bots cleared the material bar',
        );
        return true;
      }
      await this.sleep(this.gatherPollIntervalMs);
    }

    logger.warn({ jobId, timeoutMs }, 'autoGather: timed out waiting for inventories');
    return false;
  }

  private sleep(ms: number): Promise<void> {
    return new Promise((resolve) => setTimeout(resolve, ms));
  }
}
